# C5: GDA UniqueConstraint Allows NULL gene_symbol - Duplicate Records
# ============================================================================

class TestC5NullGeneSymbolDedup:
    """Verify that NULL gene_symbol records are deduplicated before upsert."""

    def test_c5_null_gene_symbol_dedup(self, db_session, sample_protein_df):
        """BUG-A-002 ROOT FIX: Multiple GDA records with empty gene_symbol
        are ALL quarantined (not coalesced to empty string and deduplicated).

        The previous test expected 1 row to survive (after dedup of the
        two empty-gene rows). That was the buggy behavior — coalescing
        empty gene_symbol to a single dedup key silently collapsed
        DISTINCT genes into one row (every empty-gene row became the
        same "" gene). The v13 fix routes empty-gene rows to a
        dead-letter table for curator review. Both rows are quarantined;
        0 rows are inserted into the GDA table."""
        from database.loaders import bulk_upsert_proteins, bulk_upsert_gda
        bulk_upsert_proteins(db_session, sample_protein_df)
        db_session.commit()
        gda_df = pd.DataFrame({
            "gene_symbol": ["", ""], "uniprot_id": ["P23219", "P23219"],
            "disease_id": ["C0001", "C0001"], "disease_name": ["Test", "Test"],
            "source": ["test", "test"], "score": [0.5, 0.6],
            "association_type": ["unknown", "unknown"], "pmid_list": [None, None],
        })
        bulk_upsert_gda(db_session, gda_df)
        db_session.commit()
        result = db_session.execute(text(
            "SELECT COUNT(*) FROM gene_disease_associations "
            "WHERE disease_id='C0001' AND source='test'"
        ))
        # Both empty-gene rows are quarantined (BUG-A-002 root fix) — 0 inserted.
        assert result.scalar() == 0, (
            f"BUG-A-002 regression: expected 0 rows in GDA table (both "
            f"empty-gene rows quarantined), got {result.scalar()}. "
            f"Coalescing to empty string silently merges distinct genes."
        )


