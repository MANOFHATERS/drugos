# ============================================================================

class TestC1PsqlMetaCommands:
    """Verify that psql meta-commands are stripped from SQL before execution."""

    def test_c1_psql_meta_commands_stripped(self):
        """Backslash-connect command should be removed from SQL content."""
        from database.migrations.run_migrations import _strip_psql_meta_commands
        sql = "\\c drug_repurposing\nBEGIN;\nCREATE TABLE t (id INT);\nCOMMIT;"
        result = _strip_psql_meta_commands(sql)
        assert "\\c" not in result
        assert "BEGIN;" in result
        assert "CREATE TABLE t" in result

    def test_c1_do_blocks_preserved(self):
        """DO $$ blocks must NOT be stripped — they are valid PostgreSQL."""
        from database.migrations.run_migrations import _strip_psql_meta_commands
        sql = "DO $$ BEGIN RAISE NOTICE 'hello'; END $$;"
        result = _strip_psql_meta_commands(sql)
        assert "DO $$" in result

    def test_c1_multiple_meta_commands_stripped(self):
        """Multiple psql meta-commands on different lines should all be removed."""
        from database.migrations.run_migrations import _strip_psql_meta_commands
        sql = "\\c drug_repurposing\n\\d table_name\nSELECT 1;"
        result = _strip_psql_meta_commands(sql)
        assert "\\c" not in result
        assert "\\d" not in result
        assert "SELECT 1" in result

    def test_c1_escaped_backslash_not_stripped(self):
        """Escaped backslashes in string literals must be preserved."""
        from database.migrations.run_migrations import _strip_psql_meta_commands
        sql = "INSERT INTO t VALUES ('\\\\path\\\\to\\\\file');"
        result = _strip_psql_meta_commands(sql)
        assert "\\\\path" in result or "path" in result


