def refresh_capabilities() -> None:
    """Re-probe RDKit and rapidfuzz; update the availability flags.

    Useful when an optional dependency is hot-installed mid-run.  Called
    from :func:`cleaning.configure` (one-line addition).

    Returns
    -------
    None
    """
    global _RDKIT_AVAILABLE, _RDKIT_VERSION, _RDKIT_INCHI_BROKEN
    global _FUZZY_AVAILABLE, _RAPIDFUZZ_VERSION

    if os.environ.get("CLEANING_SKIP_RDKIT", "0") == "1":
        if not _RDKIT_AVAILABLE:
            return
        _RDKIT_AVAILABLE = False
        return

    if not _RDKIT_AVAILABLE:
        try:
            global Chem, MolToInchi, InchiToInchiKey, CalcMolFormula
            global _RdkitDescriptors
            import rdkit as _rdkit_pkg
            from rdkit import Chem as _Chem
            from rdkit.Chem.inchi import (
                MolToInchi as _MolToInchi,
                InchiToInchiKey as _InchiToInchiKey,
            )
            from rdkit.Chem.rdMolDescriptors import (
                CalcMolFormula as _CalcMolFormula,
            )
            from rdkit.Chem import Descriptors as _Descriptors
            Chem = _Chem
            MolToInchi = _MolToInchi
            InchiToInchiKey = _InchiToInchiKey
            CalcMolFormula = _CalcMolFormula
            _RdkitDescriptors = _Descriptors
            _RDKIT_AVAILABLE = True
            _RDKIT_INCHI_BROKEN = False
            _RDKIT_VERSION = getattr(_rdkit_pkg, "__version__", "unknown")
            logger.info(
                "refresh_capabilities: RDKit %s now available",
                _RDKIT_VERSION,
            )
        except ImportError:
            pass  # still not installed

    if not _FUZZY_AVAILABLE:
        try:
            global fuzz_process, fuzz_fuzz
            import rapidfuzz as _rf
            from rapidfuzz import process as _proc, fuzz as _fuzz
            fuzz_process = _proc
            fuzz_fuzz = _fuzz
            _FUZZY_AVAILABLE = True
            _RAPIDFUZZ_VERSION = getattr(_rf, "__version__", "unknown")
            logger.info(
                "refresh_capabilities: rapidfuzz %s now available",
                _RAPIDFUZZ_VERSION,
            )
        except ImportError:
            pass


