def _log_event(
    level: str,
    event: str,
    **fields: Any,
) -> None:
    """Emit a structured log event (LOG-2).

    Parameters
    ----------
    level : str
        One of "debug", "info", "warning", "error", "critical".
    event : str
        Short event name (e.g., "smiles_conversion_failed").
    **fields : Any
        Additional structured fields to include.
    """
    log_fn = getattr(logger, level.lower(), logger.debug)
    if not logger.isEnabledFor(getattr(logging, level.upper(), logging.DEBUG)):
        return
    if _LOG_FORMAT_JSON:
        payload = {"event": event, **fields}
        try:
            from . import get_correlation_id  # type: ignore
            cid = get_correlation_id()
            if cid:
                payload["correlation_id"] = cid
        except Exception:
            pass
        log_fn("%s: %s", event, json.dumps(payload, default=str, sort_keys=True))
    else:
        # Plain-text format: "EVENT: field1=val1 field2=val2"
        parts = [f"{k}={v!r}" for k, v in fields.items()]
        log_fn("%s: %s", event, " ".join(parts))


