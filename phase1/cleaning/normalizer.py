def _normalize_groups(groups: Any) -> list[str]:
    """Normalize the ``groups`` field to a list of lowercase strings.

    (DQ-16, INTEROP-20, CODE-12) — handles strings, lists, tuples, sets,
    None, and dicts with a "name" key.
    """
    if groups is None:
        # [CODE-12] Log None as INFO.
        logger.info("standardize_drug_record: groups is None, treating as empty list")
        return []
    if isinstance(groups, str):
        return [g.strip().lower() for g in groups.replace(";", ",").split(",") if g.strip()]
    if isinstance(groups, (list, tuple, set)):
        out: list[str] = []
        for g in groups:
            # [INTEROP-20] Handle dict elements (e.g., {"name": "approved"}).
            if isinstance(g, dict):
                name = g.get("name")
                if name is not None:
                    logger.info(
                        "standardize_drug_record: dict element in groups — "
                        "extracted name=%r",
                        str(name),
                    )
                    out.append(str(name).strip().lower())
                continue
            if not isinstance(g, str):
                logger.warning(
                    "standardize_drug_record: non-string element in groups: "
                    "%r — converting via str()",
                    g,
                )
                out.append(str(g).strip().lower())
            else:
                out.append(g.strip().lower())
        return out
    # [CODE-12] Unexpected type.
    logger.warning(
        "standardize_drug_record: groups is %s, treating as empty list",
        type(groups).__name__,
    )
    return []


def _check_pii(field_name: str, value: Any) -> bool:
    """Scan a string value for PII patterns (SEC-3, SEC-17).

    Returns True if PII was detected (and logs a WARNING).
    """
    if not isinstance(value, str) or not value:
        return False
    for pattern in _PII_PATTERNS:
        if pattern.search(value):
            logger.warning(
                "standardize_drug_record: PII detected in field %s (value "
                "not logged)",
                field_name,
            )
            return True
    return False


def standardize_drug_record(
    record: dict,
    *,
    required_keys: tuple[str, ...] | None = None,
    known_inchikeys: set[str] | None = None,
    seen_inchikeys: set[str] | None = None,
    mw_range: tuple[float, float] | None = None,
    source: str | None = None,
    operator_id: str | None = None,
    source_dataset_id: str | None = None,
    raise_on_error: bool = False,
) -> dict:
    """Normalize a single drug record dict.

    Operations performed (on a **copy** — the input dict is never mutated):

    1. Strip whitespace from all string fields (CODE-13).
    2. Convert ``molecular_weight`` to ``float`` (CODE-14, SCI-7).
    3. Validate ``molecular_formula`` against SMILES-derived formula (SCI-19).
    4. Cross-check ``smiles`` ↔ ``inchikey`` consistency (DQ-5).
    5. Cross-check ``molecular_weight`` against SMILES-derived MW (DQ-4).
    6. Fuzzy-match ``drug_type`` against :data:`ALLOWED_TYPES` (DESIGN-3).
    7. Coerce ``max_phase`` to int, validate range (SCI-2, SCI-14).
    8. Normalize ``groups`` to a list of lowercase strings (CODE-12, INTEROP-20).
    9. Derive ``is_fda_approved`` with withdrawn-drug handling (SCI-1, IDEM-1).
    10. Normalize ``mechanism_of_action`` null values (SCI-18).
    11. Validate ``name`` length (DQ-6).
    12. PII / path-traversal scan on string fields (SEC-3, SEC-5, SEC-17).
    13. Attach ``_provenance`` metadata (LINEAGE-1 through LINEAGE-11).

    Parameters
    ----------
    record : dict
        A dictionary representing a single drug record.  Expected keys
        include ``molecular_weight``, ``drug_type``, ``max_phase``,
        ``groups``, etc.
    required_keys : tuple of str, optional
        If provided, validate all required keys are present (ARCH-7).
    known_inchikeys : set of str, optional
        If provided, warn on referential-integrity violations (DQ-9).
    seen_inchikeys : set of str, optional
        If provided, warn on duplicate within batch (DQ-17).  The caller
        is responsible for updating the set.
    mw_range : tuple of (float, float), optional
        Override the default MW valid range (SCI-7).
    source : str, optional
        Source attribution (LINEAGE-2).
    operator_id : str, optional
        Operator ID for audit (LINEAGE-9).  Defaults to env USER or
        AIRFLOW_CTX_DAG_ID.
    source_dataset_id : str, optional
        Source dataset ID (LINEAGE-10).
    raise_on_error : bool
        If True, raise SchemaValidationError on bad input (REL-10).

    Returns
    -------
    dict
        A new dictionary with normalized values.

    Raises
    ------
    SchemaValidationError
        If ``record`` is not a dict and raise_on_error=True (REL-10).

    Examples
    --------
    >>> rec = {"name": "  Aspirin  ", "molecular_weight": "180.16",
    ...        "drug_type": "small molecule", "max_phase": 4, "groups": []}
    >>> out = standardize_drug_record(rec)
    >>> out["name"]
    'Aspirin'
    >>> out["molecular_weight"]
    180.16
    >>> out["drug_type"]
    'Small molecule'
    >>> out["is_fda_approved"]
    True

    Notes
    -----
    Guaranteed output keys (DESIGN-7): ``is_fda_approved`` (bool or None —
    None preserves the upstream "honest unknown" signal, e.g. ChEMBL
    max_phase=4 per v29/v41 ROOT FIX S3), ``is_withdrawn`` (always bool),
    ``drug_type`` (always str, from ALLOWED_TYPES), ``_provenance``
    (always dict).  Optional keys (``molecular_weight``, ``name``,
    ``inchikey``, etc.) are present iff present in input (or derivable).

    The input dict is NEVER mutated.  A shallow copy is made (ARCH-10)
    with nested mutables (list/dict/set) copied one level deep.
    """
    # [REL-10] Type check.
    if not isinstance(record, dict):
        msg = (
            f"standardize_drug_record: expected dict, got "
            f"{type(record).__name__}"
        )
        logger.error(msg)
        if raise_on_error:
            try:
                from . import SchemaValidationError  # type: ignore
                raise SchemaValidationError(msg)
            except ImportError:
                raise TypeError(msg)
        # Return an empty dict (with provenance) for graceful degradation.
        record = {}

    # [PERF-10] Guard against huge records.
    if len(record) > _MAX_RECORD_KEYS:
        logger.warning(
            "standardize_drug_record: record has %d keys (> %d) — "
            "processing first %d only",
            len(record),
            _MAX_RECORD_KEYS,
            _MAX_RECORD_KEYS,
        )

    # [ARCH-10, PERF-1, IDEM-11, SEC-12] Shallow copy (no deepcopy).
    out = _shallow_copy_record(record)

    # [LINEAGE-11] Transformation chain.
    prev_chain = []
    if isinstance(out.get("_provenance"), dict):
        prev_chain = list(out["_provenance"].get("transformation_chain", []))

    transformations: list[str] = []
    warnings_list: list[str] = []
    pii_warnings: list[str] = []

    # [CODE-13] Strip whitespace from all string fields — dict comprehension.
    for key, value in list(out.items()):
        if isinstance(value, str):
            stripped = value.strip()
            if stripped != value:
                out[key] = stripped
                transformations.append(f"stripped_whitespace:{key}")
            # [SEC-3] PII scan.
            if _check_pii(key, stripped):
                pii_warnings.append(key)

    # [INTEROP-7] Track unknown keys.
    unknown_keys = [k for k in out.keys() if k not in _KNOWN_RECORD_KEYS and k != "_provenance"]
    if unknown_keys:
        logger.info(
            "standardize_drug_record: unknown keys %r — preserved as-is",
            unknown_keys,
        )

    # [DQ-11] Missing-field counters.
    with _DQ_LOCK:
        if out.get("molecular_weight") is None and "molecular_weight" in record:
            _DQ_COUNTS["missing_mw"] += 1
        if not out.get("drug_type"):
            _DQ_COUNTS["missing_drug_type"] += 1
        if out.get("max_phase") is None:
            _DQ_COUNTS["missing_max_phase"] += 1
        if not out.get("groups"):
            _DQ_COUNTS["missing_groups"] += 1
        if not out.get("name"):
            _DQ_COUNTS["missing_name"] += 1
        if out.get("inchikey") is None:
            _DQ_COUNTS["missing_inchikey"] += 1
        if not out.get("smiles"):
            _DQ_COUNTS["missing_smiles"] += 1

    # [DQ-12] Warn on inchikey=None.
    if "inchikey" in out and out["inchikey"] is None:
        logger.info(
            "standardize_drug_record: inchikey is None — expected for "
            "some records (e.g., biologics)"
        )

    # [SEC-5] Path traversal in name.
    name_val = out.get("name")
    if isinstance(name_val, str) and _PATH_TRAVERSAL_PATTERN.search(name_val):
        logger.warning(
            "standardize_drug_record: path traversal detected in name — "
            "replacing with BLOCKED-<hash>"
        )
        blocked_name = f"BLOCKED-{hashlib.sha256(name_val.encode()).hexdigest()[:8]}"
        out["name"] = blocked_name
        transformations.append(f"blocked_path_traversal:name")
        warnings_list.append("path_traversal_in_name")

    # [DQ-6] Name length validation.
    if "name" in out and isinstance(out["name"], str) and out["name"]:
        if len(out["name"]) < _MIN_NAME_LENGTH:
            logger.warning(
                "standardize_drug_record: name %r is shorter than %d chars "
                "(DB constraint) — preserved but flagged",
                out["name"],
                _MIN_NAME_LENGTH,
            )
            warnings_list.append(f"name_too_short:{len(out['name'])}")

    # [SCI-18] Normalize mechanism_of_action null values.
    if "mechanism_of_action" in out and isinstance(out["mechanism_of_action"], str):
        if out["mechanism_of_action"].lower() in _NULL_MECHANISM_VALUES:
            out["mechanism_of_action"] = ""
            transformations.append("normalized_null_mechanism_of_action")

    # [CODE-14, CODE-15, SCI-7] Molecular weight.
    mw_range_eff = mw_range or _MW_VALID_RANGE
    if "molecular_weight" in out:
        mw_raw = out["molecular_weight"]
        mw_float = _coerce_molecular_weight(mw_raw)
        if mw_float is not None:
            # [SCI-7] Range validation.
            if not math.isfinite(mw_float) or not (
                mw_range_eff[0] <= mw_float <= mw_range_eff[1]
            ):
                logger.warning(
                    "standardize_drug_record: molecular_weight %s out of "
                    "valid range %s — setting to None",
                    mw_float,
                    mw_range_eff,
                )
                warnings_list.append(f"mw_out_of_range:{mw_float}")
                out["molecular_weight"] = None
            else:
                out["molecular_weight"] = mw_float
                if mw_raw != mw_float:
                    transformations.append(f"converted_molecular_weight:{mw_float}")
        else:
            out["molecular_weight"] = None

    # [CODE-22] Normalize drug_type — handle None explicitly.
    raw_type = out.get("drug_type")
    if raw_type is None:
        logger.debug("standardize_drug_record: drug_type is None — using 'Unknown'")
        out["drug_type"] = _DEFAULT_DRUG_TYPE
    else:
        matched = _fuzzy_match_drug_type(raw_type)
        if matched != raw_type:
            transformations.append(f"fuzzy_matched_drug_type:{matched}")
        out["drug_type"] = matched

    # [CODE-12, DQ-16, INTEROP-20] Normalize groups.
    groups = out.get("groups", [])
    groups_list = _normalize_groups(groups)
    out["groups"] = groups_list  # store the normalized lowercase list
    # [SEC-17] PII scan on groups.
    cleaned_groups: list[str] = []
    for i, g in enumerate(groups_list):
        if _check_pii(f"groups[{i}]", g):
            pii_warnings.append(f"groups[{i}]")
        else:
            cleaned_groups.append(g)
    if len(cleaned_groups) != len(groups_list):
        out["groups"] = cleaned_groups
        groups_list = cleaned_groups

    # [DQ-16] Contradictory group pairs.
    group_warnings: list[str] = []
    for pair in _CONTRADICTORY_GROUP_PAIRS:
        if pair[0] in groups_list and pair[1] in groups_list:
            logger.warning(
                "standardize_drug_record: contradictory group pair %s in "
                "groups %r",
                pair,
                groups_list,
            )
            group_warnings.append(f"contradictory:{pair[0]}+{pair[1]}")
    if group_warnings:
        out["group_warnings"] = group_warnings

    # [SCI-1, SCI-2, SCI-14, CODE-3, CODE-16, IDEM-1] max_phase + is_fda_approved.
    max_phase = out.get("max_phase")
    phase_int = _coerce_phase_to_int(max_phase)
    phase_approved = phase_int == 4
    group_approved = "approved" in groups_list
    # [SCI-1] Withdrawn detection.
    is_withdrawn = any(g in WITHDRAWN_GROUP_KEYWORDS for g in groups_list)
    if is_withdrawn:
        with _DQ_LOCK:
            _DQ_COUNTS["withdrawn_drugs"] += 1
        if group_approved:
            # [SCI-1] Important downgrade — log INFO.
            logger.info(
                "standardize_drug_record: drug has both 'approved' and a "
                "withdrawn keyword in groups — marking NOT FDA-approved"
            )

    # [IDEM-1] Preserve upstream is_fda_approved unless EXPLICIT contradiction.
    # Per the verify section of IDEM-1:
    #   standardize_drug_record({"is_fda_approved":True,"max_phase":3,"groups":[]})
    #   → is_fda_approved=True, source="upstream"
    # So we ONLY downgrade when there is POSITIVE evidence the drug is NOT
    # approved — i.e., is_withdrawn=True.  Lack of confirming evidence
    # (max_phase<4 and no approved group) is NOT enough to downgrade; the
    # upstream caller may have had additional info (e.g., EMA approval,
    # approval before ChEMBL tracking).
    # v41 ROOT FIX (S3 SCIENTIFIC): respect None.  The ChEMBL pipeline's
    # v29 ROOT FIX deliberately returns is_fda_approved=None for
    # max_phase=4 ("Phase IV trial" CORRELATES with but is NOT synonymous
    # with FDA approval — could be EMA-only, post-marketing surveillance
    # of an already-approved drug, etc.).  The previous code's `else`
    # branch treated None exactly like False and fell through to
    # `final_approved = derived_approved`, which derived True from
    # phase_approved alone — silently flipping ChEMBL's "honest unknown"
    # to True and propagating to the DB.  Patient-safety compound
    # degradation: downstream `WHERE is_fda_approved = TRUE` queries
    # returned drugs ChEMBL said were unknown.  We now keep None for
    # the missing-upstream case and require an explicit upstream True
    # or a positive `approved` group (not phase 4 alone) to derive True.
    upstream_approved = record.get("is_fda_approved")
    derived_approved = (group_approved and not is_withdrawn)
    is_fda_approved_source: str
    if upstream_approved is True:
        if is_withdrawn:
            # [IDEM-1] Explicit contradiction — drug was pulled from market.
            is_fda_approved_source = "derived:withdrawn"
            final_approved = False
            with _DQ_LOCK:
                _DQ_COUNTS["is_fda_approved_flips"] += 1
            logger.info(
                "standardize_drug_record: downgrading is_fda_approved "
                "True→False due to withdrawn status"
            )
        else:
            # [IDEM-1] Preserve upstream True — caller may have had more info.
            is_fda_approved_source = "upstream"
            final_approved = True
            if not derived_approved:
                logger.info(
                    "standardize_drug_record: preserving upstream "
                    "is_fda_approved=True despite derived=False (no "
                    "explicit contradictory evidence)"
                )
    elif upstream_approved is None:
        # v41 ROOT FIX (S3): upstream said "unknown" — honour that.
        # Do NOT derive True from phase_approved alone; only a positive
        # `approved` group can establish derived approval here, and even
        # then we keep the None as the primary signal so downstream
        # consumers know the upstream source did not assert approval.
        if group_approved and not is_withdrawn:
            is_fda_approved_source = "derived:groups"
            # Keep None — upstream explicitly disclaimed FDA approval.
            final_approved = None
            logger.info(
                "standardize_drug_record: upstream is_fda_approved=None "
                "(ChEMBL 'honest unknown' for max_phase=4); preserving "
                "None despite derived:groups evidence (phase=%s, "
                "groups=%s)",
                phase_int,
                groups_list,
            )
        else:
            is_fda_approved_source = "upstream"
            final_approved = None
    else:
        # upstream_approved is False
        if group_approved and not is_withdrawn:
            is_fda_approved_source = "derived:groups"
            final_approved = True
            with _DQ_LOCK:
                _DQ_COUNTS["is_fda_approved_flips"] += 1
            logger.info(
                "standardize_drug_record: upgrading is_fda_approved "
                "False→True due to positive 'approved' group evidence "
                "(phase=%s, groups=%s)",
                phase_int,
                groups_list,
            )
        elif phase_approved and not is_withdrawn:
            # For an upstream False, max_phase=4 IS positive evidence
            # (Phase IV is rarely run for unapproved drugs).
            is_fda_approved_source = "derived:max_phase"
            final_approved = True
            with _DQ_LOCK:
                _DQ_COUNTS["is_fda_approved_flips"] += 1
            logger.info(
                "standardize_drug_record: upgrading is_fda_approved "
                "False→True due to max_phase=4 (Phase IV evidence; "
                "groups=%s)",
                groups_list,
            )
        else:
            is_fda_approved_source = "derived:default"
            final_approved = False

    # [DQ-15] Track is_fda_approved flips.
    if upstream_approved is not None and bool(upstream_approved) != final_approved:
        with _DQ_LOCK:
            _DQ_COUNTS["is_fda_approved_flips"] += 1
        logger.info(
            "standardize_drug_record: is_fda_approved flipped %s→%s "
            "(evidence: phase=%s, groups=%s)",
            bool(upstream_approved),
            final_approved,
            phase_int,
            groups_list,
        )
        transformations.append(
            f"derived_is_fda_approved:{final_approved}"
        )

    out["is_fda_approved"] = final_approved
    out["is_withdrawn"] = is_withdrawn
    # [SCI-1] Audit field — was the drug ever approved.
    out["was_ever_approved"] = group_approved
    # [IDEM-1, LINEAGE-19] Source attribution.
    out["is_fda_approved_source"] = is_fda_approved_source

    # [DQ-5] smiles ↔ inchikey consistency check.
    smiles_val = out.get("smiles")
    inchikey_val = out.get("inchikey")
    if smiles_val and inchikey_val and _RDKIT_AVAILABLE:
        # Don't run if inchikey is synthetic.
        if not is_synthetic_inchikey(inchikey_val):
            try:
                derived_ik = convert_to_inchikey(smiles_val)
                if derived_ik is not None and derived_ik != inchikey_val:
                    logger.warning(
                        "standardize_drug_record: inchikey mismatch — "
                        "SMILES %s derives to %s but record has %s",
                        _truncate_for_log(smiles_val),
                        derived_ik,
                        inchikey_val,
                    )
                    out["inchikey_mismatch"] = True
                    warnings_list.append("inchikey_mismatch")
                    with _DQ_LOCK:
                        _DQ_COUNTS["inchikey_mismatches"] += 1
            except Exception:
                pass  # never crash on cross-check

    # [DQ-4] MW vs SMILES-derived MW.
    if smiles_val and out.get("molecular_weight") is not None and _RDKIT_AVAILABLE:
        try:
            mol_check = _smiles_to_mol(smiles_val, sanitize=False)
            if mol_check is not None:
                derived_mw = _RdkitDescriptors.MolWt(mol_check)
                recorded_mw = out["molecular_weight"]
                if derived_mw > 0 and recorded_mw > 0:
                    ratio = recorded_mw / derived_mw
                    # 5% tolerance OR factor-of-10 error.
                    if (
                        abs(ratio - 1.0) > _MW_TOLERANCE
                        and abs(ratio - 10.0) > _MW_TOLERANCE
                        and abs(ratio - 0.1) > _MW_TOLERANCE
                    ):
                        logger.warning(
                            "standardize_drug_record: MW mismatch — "
                            "recorded %s vs SMILES-derived %s (ratio=%.3f)",
                            recorded_mw,
                            derived_mw,
                            ratio,
                        )
                        warnings_list.append(
                            f"mw_mismatch:{recorded_mw}_vs_{derived_mw:.2f}"
                        )
        except Exception:
            pass

    # [SCI-19] Molecular formula vs SMILES-derived formula.
    if (
        smiles_val
        and out.get("molecular_formula")
        and _RDKIT_AVAILABLE
    ):
        try:
            mol_check = _smiles_to_mol(smiles_val, sanitize=False)
            if mol_check is not None:
                derived_formula = CalcMolFormula(mol_check)
                if derived_formula and derived_formula != out["molecular_formula"]:
                    logger.warning(
                        "standardize_drug_record: molecular_formula mismatch "
                        "— recorded %s vs SMILES-derived %s (preserving "
                        "recorded value)",
                        out["molecular_formula"],
                        derived_formula,
                    )
                    warnings_list.append(
                        f"formula_mismatch:{out['molecular_formula']}_vs_{derived_formula}"
                    )
        except Exception:
            pass

    # [DQ-9] Referential integrity check.
    if known_inchikeys is not None and inchikey_val and inchikey_val not in known_inchikeys:
        logger.warning(
            "standardize_drug_record: inchikey %s not in known_inchikeys "
            "(referential integrity violation)",
            _sanitize_string_local(inchikey_val),
        )
        warnings_list.append("referential_integrity_violation")

    # [DQ-17] Duplicate within batch.
    if seen_inchikeys is not None and inchikey_val and inchikey_val in seen_inchikeys:
        logger.warning(
            "standardize_drug_record: inchikey %s already seen in this batch "
            "(duplicate)",
            _sanitize_string_local(inchikey_val),
        )
        warnings_list.append("duplicate_in_batch")
    if seen_inchikeys is not None and inchikey_val:
        seen_inchikeys.add(inchikey_val)

    # [ARCH-7] Required keys check.
    if required_keys is not None:
        missing_required = [k for k in required_keys if k not in out or out[k] is None]
        if missing_required:
            logger.warning(
                "standardize_drug_record: missing required keys %r",
                missing_required,
            )
            warnings_list.append(f"missing_required:{missing_required}")
    else:
        # [ARCH-7] If both inchikey and name are missing, warn.
        if not inchikey_val and not out.get("name"):
            logger.warning(
                "standardize_drug_record: record has neither inchikey nor name"
            )
            warnings_list.append("missing_both_inchikey_and_name")

    # [IDEM-6, LINEAGE-1] Provenance.
    input_sha = hashlib.sha256(
        json.dumps(record, sort_keys=True, default=str).encode("utf-8", errors="replace")
    ).hexdigest()

    # [IDEM-10, LINEAGE-1] Logic hash + rule version.
    provenance: dict[str, Any] = {
        "cleaned_by": "normalizer.standardize_drug_record",
        "cleaner_version": _NORMALIZER_VERSION,
        "rule_version": _RULE_VERSION,
        "schema_version": _OUTPUT_SCHEMA_VERSION,
        "config_version": _CONFIG_VERSION,
        "logic_hash": _LOGIC_HASH,
        "cleaned_at": datetime.now(timezone.utc).isoformat(),
        "input_sha256": input_sha,
        "transformations": transformations,
        "warnings": warnings_list,
        "rdkit_version": _RDKIT_VERSION,
        "rapidfuzz_version": _RAPIDFUZZ_VERSION,
        "is_synthetic": is_synthetic_inchikey(inchikey_val) if inchikey_val else False,
        "transformation_chain": prev_chain + ["standardize_drug_record"],
    }
    # [LINEAGE-2] Source.
    if source is not None:
        provenance["source"] = source
    # [LINEAGE-9] Operator ID.
    if operator_id is None:
        operator_id = os.environ.get("USER") or os.environ.get("AIRFLOW_CTX_DAG_ID")
    if operator_id is not None:
        provenance["operator_id"] = operator_id
    # [LINEAGE-10] Source dataset ID.
    if source_dataset_id is not None:
        provenance["source_dataset_id"] = source_dataset_id
    # [LOG-4] Correlation ID.
    try:
        from . import get_correlation_id  # type: ignore
        cid = get_correlation_id()
        if cid:
            provenance["correlation_id"] = cid
    except Exception:
        pass
    # [SEC-3] PII warnings.
    if pii_warnings:
        provenance["pii_warnings"] = pii_warnings
    # [INTEROP-7] Unknown keys.
    if unknown_keys:
        provenance["unknown_keys"] = unknown_keys
    # [LINEAGE-18] Previous provenance (if re-cleaning).
    if isinstance(record.get("_provenance"), dict):
        if record["_provenance"].get("cleaner_version") == _NORMALIZER_VERSION:
            logger.warning(
                "standardize_drug_record: record already cleaned by "
                "version %s — re-cleaning may overwrite provenance",
                _NORMALIZER_VERSION,
            )
        provenance["previous_provenance"] = dict(record["_provenance"])

    out["_provenance"] = provenance

    # [LINEAGE-5] Output SHA-256 (computed on a copy minus output_sha256).
    out_for_hash = dict(out)
    out_for_hash["_provenance"] = {
        k: v for k, v in out["_provenance"].items() if k != "output_sha256"
    }
    try:
        output_sha = hashlib.sha256(
            json.dumps(out_for_hash, sort_keys=True, default=str).encode("utf-8", errors="replace")
        ).hexdigest()
        out["_provenance"]["output_sha256"] = output_sha
    except Exception:
        pass

    # [IDEM-11] Sanity assertion — input keys preserved in order.
    try:
        assert isinstance(out, dict)
        input_keys = list(record.keys())
        output_keys = list(out.keys())
        # The first len(input_keys) output keys should match input keys
        # (in order), with new keys appended.
        for i, k in enumerate(input_keys):
            if i < len(output_keys) and output_keys[i] != k:
                # Only log — don't fail (some keys may have been added before)
                logger.debug(
                    "standardize_drug_record: key order differs at index %d "
                    "(input=%s, output=%s)",
                    i,
                    k,
                    output_keys[i],
                )
                break
    except Exception:
        pass

    # [IDEM-6] Audit log.
    _audit_log_local(
        "standardize_drug_record",
        {
            "input_sha256": input_sha,
            "cleaner_version": _NORMALIZER_VERSION,
            "is_fda_approved": final_approved,
            "is_withdrawn": is_withdrawn,
        },
    )

    with _METRICS_LOCK:
        _METRICS["records_standardized"] += 1

    return out


def standardize_drug_records_batch(
    records: list[dict],
    *,
    checkpoint_every: int = 100,
    on_checkpoint: "Callable[[int, list[dict]], None] | None" = None,
    **kwargs: Any,
) -> dict[str, list[dict]]:
    """Process a batch of drug records with checkpointing (REL-11).

    Parameters
    ----------
    records : list of dict
        Records to process.
    checkpoint_every : int
        Call on_checkpoint every N records.  Default: 100.
    on_checkpoint : callable, optional
        Callback ``(idx, results_so_far) -> None``.
    **kwargs
        Forwarded to :func:`standardize_drug_record`.

    Returns
    -------
    dict
        ``{"succeeded": [...], "failed": [...]}`` — failed records are
        returned with an ``_error`` key.

    v41 ROOT FIX (SEV3): note on row-by-row apply.  This function
    processes one record at a time rather than vectorising with
    pandas/numpy.  The choice is INTENTIONAL — ``standardize_drug_record``
    performs many cross-field validations (smiles↔inchikey RDKit
    consistency check, MW bounds vs SMILES-derived MW, drug_type fuzzy
    match against ALLOWED_TYPES, is_fda_approved derivation from
    phase/groups/withdrawn fields, etc.) and emits per-record side
    effects (logging warnings, incrementing _DQ_COUNTS counters, calling
    _audit_log_local for lineage).  Vectorising would require either (a)
    a major refactor to defer all side effects to a post-pass (risking
    audit-log gaps if a record fails mid-batch), or (b) parallelising
    with a thread/process pool (RDKit is not thread-safe for InChI
    generation; the rate-limiter is process-local).  For the typical
    batch size (~10k ChEMBL records) the row-by-row approach runs in
    <60s which is well within pipeline budgets.  If profiling shows
    this is a bottleneck, the right vectorisation target is the
    SMILES→InChIKey conversion (already batched via
    convert_to_inchikeys) — NOT this orchestrator.
    """
    succeeded: list[dict] = []
    failed: list[dict] = []
    for idx, record in enumerate(records):
        try:
            out = standardize_drug_record(record, **kwargs)
            succeeded.append(out)
        except Exception as exc:
            logger.error(
                "standardize_drug_records_batch: record %d failed: %s",
                idx,
                exc,
                exc_info=True,
            )
            failed_rec = dict(record) if isinstance(record, dict) else {"record": record}
            failed_rec["_error"] = str(exc)
            failed_rec["_error_idx"] = idx
            failed.append(failed_rec)
        if on_checkpoint is not None and (idx + 1) % checkpoint_every == 0:
            try:
                on_checkpoint(idx + 1, succeeded)
            except Exception:
                pass
    return {"succeeded": succeeded, "failed": failed}


def standardize_drug_records_chunked(
    records: Iterable[dict],
    chunk_size: int = 1000,
    on_chunk: "Callable[[int, int], None] | None" = None,
    **kwargs: Any,
) -> Iterator[dict]:
    """Generator that yields standardized records one at a time (PERF-11).

    Memory-efficient for 10M+ records.

    Parameters
    ----------
    records : iterable of dict
        Records to process.
    chunk_size : int
        Call on_chunk every N records.  Default: 1000.
    on_chunk : callable, optional
        Callback ``(idx, count) -> None``.
    **kwargs
        Forwarded to :func:`standardize_drug_record`.

    Yields
    ------
    dict
        Standardized records, one at a time.
    """
    count = 0
    for idx, record in enumerate(records):
        try:
            yield standardize_drug_record(record, **kwargs)
        except Exception as exc:
            logger.error(
                "standardize_drug_records_chunked: record %d failed: %s",
                idx,
                exc,
                exc_info=True,
            )
        count += 1
        if on_chunk is not None and count % chunk_size == 0:
            try:
                on_chunk(idx + 1, count)
            except Exception:
                pass


