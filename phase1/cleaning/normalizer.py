def _smiles_to_mol(smiles: str, *, sanitize: bool = True):
    """Parse a SMILES string into an RDKit Mol object (SCI-4, SCI-13).

    Tries default sanitization first; on failure, tries sanitize=False
    + partial sanitization (recovers hypervalent sulfur, unusual
    aromaticity, etc.).

    Parameters
    ----------
    smiles : str
        The SMILES string (already validated for length/chars).
    sanitize : bool
        If True (default), use full sanitization.  If False, parse only.

    Returns
    -------
    rdkit.Chem.Mol or None
        The parsed Mol, or None if both paths fail.
    """
    if not _RDKIT_AVAILABLE:
        return None
    # [SCI-4] First attempt: default sanitization
    mol = Chem.MolFromSmiles(smiles)
    if mol is not None:
        return mol
    if not sanitize:
        return None
    # [SCI-4] Fallback: sanitize=False + partial sanitize
    try:
        mol = Chem.MolFromSmiles(smiles, sanitize=False)
        if mol is None:
            return None
        Chem.SanitizeMol(
            mol,
            sanitizeOps=Chem.SanitizeFlags.SANITIZE_ALL
            ^ Chem.SanitizeFlags.SANITIZE_PROPERTIES,
        )
        logger.info(
            "convert_to_inchikey: used sanitize=False fallback for SMILES %s",
            _truncate_for_log(smiles),
        )
        return mol
    except Exception as exc:
        # [CODE-1] Specific RDKit exceptions only; re-raise others.
        if isinstance(
            exc,
            (
                Chem.AtomValenceException,
                Chem.KekulizeException,
                Chem.AtomKekulizeException,
                Chem.ValenceException,
            ),
        ):
            logger.debug(
                "convert_to_inchikey: sanitize=False fallback failed for "
                "SMILES %s: %s",
                _truncate_for_log(smiles),
                exc,
            )
            return None
        logger.error(
            "convert_to_inchikey: unexpected error in sanitize=False "
            "fallback for SMILES %s",
            _truncate_for_log(smiles),
            exc_info=True,
        )
        return None


def _validate_inchikey_version_char(
    key: str,
) -> Literal["S", "N", None]:
    """Return the version character of a 27-char InChIKey (SCI-8).

    Parameters
    ----------
    key : str
        A 27-char InChIKey.

    Returns
    -------
    "S", "N", or None
        "S" for standard, "N" for non-standard, None if the last char is
        neither.
    """
    if len(key) != 27:
        return None
    last = key[-1]
    if last == "S":
        return "S"
    if last == "N":
        return "N"
    return None


