def _set_unit_conversion(key: str, value: float) -> None:
    """Mutator for _UNIT_CONVERSIONS (IDEM-15, SEC-7).

    Validates that ``value`` is a finite number.  Rejects NaN/inf.

    Parameters
    ----------
    key : str
        Unit key (e.g., "fM").
    value : float
        Conversion factor to nM.

    Raises
    ------
    ValueError
        If value is not a finite number.
    """
    if not isinstance(value, (int, float)) or not math.isfinite(float(value)):
        raise ValueError(
            f"_set_unit_conversion: value must be a finite number, got {value!r}"
        )
    _UNIT_CONVERSIONS[key] = float(value)
    # [DESIGN-5] Update casefolded lookup too.
    _UNIT_CONVERSIONS_CF[key.casefold()] = float(value)


def _set_allowed_types(new_types: list[str]) -> None:
    """Replace ALLOWED_TYPES (CFG-3).

    Parameters
    ----------
    new_types : list of str
        New list of allowed drug types.
    """
    global ALLOWED_TYPES, _ALLOWED_TYPES_TUPLE, _ALLOWED_TYPES_LOWER
    ALLOWED_TYPES = list(new_types)
    _ALLOWED_TYPES_TUPLE = tuple(ALLOWED_TYPES)
    _ALLOWED_TYPES_LOWER = {t.lower(): t for t in ALLOWED_TYPES}


def _validate_config(key: str, value: Any) -> None:
    """Validate a single config key/value (CFG-4).

    Raises ValueError on invalid input.
    """
    if key == "fuzzy_threshold":
        if not isinstance(value, (int, float)) or not (0.0 <= float(value) <= 1.0):
            raise ValueError(
                f"fuzzy_threshold must be in [0, 1], got {value!r}"
            )
    elif key == "fuzzy_scorer":
        if value not in _FUZZY_SCORERS:
            raise ValueError(
                f"fuzzy_scorer must be one of {sorted(_FUZZY_SCORERS)}, got {value!r}"
            )
    elif key == "allowed_types":
        if not isinstance(value, list) or not all(
            isinstance(x, str) and x for x in value
        ):
            raise ValueError(
                "allowed_types must be a list of non-empty strings"
            )
    elif key == "unit_conversions":
        if not isinstance(value, dict):
            raise ValueError("unit_conversions must be a dict")
        for k, v in value.items():
            if not isinstance(k, str):
                raise ValueError(f"unit_conversions key must be str, got {type(k).__name__}")
            if not isinstance(v, (int, float)) or not math.isfinite(float(v)):
                raise ValueError(
                    f"unit_conversions['{k}'] must be a finite number, got {v!r}"
                )
    elif key == "stereo_policy":
        if value not in ("preserve", "ignore"):
            raise ValueError(
                f"stereo_policy must be 'preserve' or 'ignore', got {value!r}"
            )
    elif key == "rate_limit_per_sec":
        if value is not None and (not isinstance(value, (int, float)) or value <= 0):
            raise ValueError(
                f"rate_limit_per_sec must be a positive number or None, got {value!r}"
            )
    else:
        # Unknown keys are not errors — they're just ignored.
        logger.debug("_validate_config: unknown key %r (ignored)", key)


def configure_normalizer(
    *,
    fuzzy_threshold: float | None = None,
    fuzzy_scorer: str | None = None,
    allowed_types: list[str] | None = None,
    unit_conversions: dict[str, float] | None = None,
    stereo_policy: Literal["preserve", "ignore"] | None = None,
    # v34 ROOT FIX (HIGH #5): use sentinel default instead of None so we
    # can distinguish "caller didn't pass" (leave existing limiter alone)
    # from "caller explicitly passed None" (disable the limiter).
    rate_limit_per_sec: Any = _RATE_LIMIT_UNSET,
    openlineage_emitter: "Callable[[dict], None] | None" = None,
) -> None:
    """Override module-level configuration (CFG-1 through CFG-16).

    All parameters are optional; only the ones provided are updated.

    Parameters
    ----------
    fuzzy_threshold : float, optional
        Minimum similarity score [0, 1] for drug type fuzzy matching.
    fuzzy_scorer : str, optional
        Scorer name: "WRatio", "QRatio", "token_sort_ratio",
        "token_set_ratio", "partial_ratio".
    allowed_types : list of str, optional
        REPLACE the default ALLOWED_TYPES (use extend instead — CFG-3).
    unit_conversions : dict, optional
        MERGE with the default _UNIT_CONVERSIONS.
    stereo_policy : "preserve" or "ignore", optional
        Whether to preserve or ignore stereochemistry in InChIKey generation.
    rate_limit_per_sec : float, optional
        Max convert_to_inchikey calls per second.  None = no limit.
    openlineage_emitter : callable, optional
        Callback for OpenLineage events (LINEAGE-16).

    Raises
    ------
    ValueError
        If any value is invalid (CFG-4).
    """
    global _FUZZY_THRESHOLD, _fuzzy_scorer_name, STEREO_POLICY
    global _rate_limiter, _openlineage_emitter

    # [CFG-12] Snapshot for diff logging.
    snapshot_before = {
        "fuzzy_threshold": _FUZZY_THRESHOLD,
        "fuzzy_scorer": _fuzzy_scorer_name,
        "stereo_policy": STEREO_POLICY,
        "rate_limit_per_sec": (
            getattr(_rate_limiter, "_rate", None) if _rate_limiter else None
        ),
    }

    if fuzzy_threshold is not None:
        _validate_config("fuzzy_threshold", fuzzy_threshold)
        _FUZZY_THRESHOLD = float(fuzzy_threshold)
        # [CODE-17] Clear the LRU cache since the threshold doesn't affect
        # cached SMILES conversions (no cache clear needed for fuzzy_threshold)
        logger.info("configure_normalizer: fuzzy_threshold=%.4f", _FUZZY_THRESHOLD)

    if fuzzy_scorer is not None:
        _validate_config("fuzzy_scorer", fuzzy_scorer)
        _fuzzy_scorer_name = fuzzy_scorer
        logger.info("configure_normalizer: fuzzy_scorer=%s", _fuzzy_scorer_name)

    if allowed_types is not None:
        _validate_config("allowed_types", allowed_types)
        # [CFG-3] EXTEND the default, don't replace (backward compat).
        merged = list(_DEFAULT_ALLOWED_TYPES)
        for t in allowed_types:
            if t not in merged:
                merged.append(t)
        _set_allowed_types(merged)
        logger.info(
            "configure_normalizer: extended ALLOWED_TYPES to %d entries",
            len(ALLOWED_TYPES),
        )

    if unit_conversions is not None:
        _validate_config("unit_conversions", unit_conversions)
        for k, v in unit_conversions.items():
            _set_unit_conversion(k, v)
        logger.info(
            "configure_normalizer: merged %d unit_conversions (total=%d)",
            len(unit_conversions),
            len(_UNIT_CONVERSIONS),
        )

    if stereo_policy is not None:
        _validate_config("stereo_policy", stereo_policy)
        STEREO_POLICY = stereo_policy
        # [CODE-17] Clear the LRU cache since stereo_policy affects output.
        try:
            _convert_to_inchikey_cached.cache_clear()
        except Exception:
            pass
        logger.info("configure_normalizer: stereo_policy=%s", STEREO_POLICY)

    # v34 ROOT FIX (HIGH #5): the previous code was:
    #   if rate_limit_per_sec is not None:
    #       ... set up limiter ...
    #   elif rate_limit_per_sec is None and "rate_limit_per_sec" in kwargs_dict():
    #       _rate_limiter = None
    # Because the default was None, ANY call without an explicit
    # rate_limit_per_sec hit the elif branch and DISABLED the limiter.
    # The new logic uses sentinels:
    #   - _RATE_LIMIT_UNSET (default): caller didn't pass it → do nothing
    #   - None: caller explicitly passed None → disable limiter
    #   - float: caller passed a rate → set up limiter
    if rate_limit_per_sec is _RATE_LIMIT_UNSET:
        # Caller didn't touch the rate limiter — leave it as-is.
        pass
    elif rate_limit_per_sec is None:
        # Caller explicitly passed None → disable the limiter.
        # v41 ROOT FIX (SEV3): the previous comment claimed Python
        # treats a second ``global`` declaration as a SyntaxError.
        # That is FALSE — Python (language reference §8.3, "The global
        # statement") explicitly permits multiple ``global`` statements
        # for the same name within a single function (they are
        # idempotent).  The original v35 fix removed the redundant
        # declaration (which is good code hygiene) but justified it
        # with a non-existent language rule.  We keep the removal (one
        # declaration is cleaner) but replace the misleading rationale
        # with the real one: a single function-level ``global``
        # declaration already covers the whole function scope, so a
        # second one is redundant — not a SyntaxError.
        _rate_limiter = None
        logger.warning(
            "configure_normalizer: rate_limit_per_sec explicitly set "
            "to None — rate limiting DISABLED. This is INSECURE for "
            "production. (v34 root fix HIGH #5)"
        )
    else:
        _validate_config("rate_limit_per_sec", rate_limit_per_sec)
        # [SEC-13] Token bucket.
        rate = float(rate_limit_per_sec)
        interval = 1.0 / rate
        last_call = [time.monotonic()]
        lock = threading.Lock()

        def _limiter() -> bool:
            with lock:
                now = time.monotonic()
                if now - last_call[0] < interval:
                    return False
                last_call[0] = now
                return True

        _limiter._rate = rate  # type: ignore[attr-defined]
        _rate_limiter = _limiter
        logger.info("configure_normalizer: rate_limit_per_sec=%.2f", rate)

    if openlineage_emitter is not None:
        _openlineage_emitter = openlineage_emitter
        logger.info("configure_normalizer: openlineage_emitter set")

    # [ARCH-4, IDEM-9] Refresh capabilities.
    refresh_capabilities()

    # [CFG-12] Diff logging.
    snapshot_after = {
        "fuzzy_threshold": _FUZZY_THRESHOLD,
        "fuzzy_scorer": _fuzzy_scorer_name,
        "stereo_policy": STEREO_POLICY,
        "rate_limit_per_sec": (
            getattr(_rate_limiter, "_rate", None) if _rate_limiter else None
        ),
    }
    diffs = {
        k: (snapshot_before.get(k), snapshot_after.get(k))
        for k in snapshot_after
        if snapshot_before.get(k) != snapshot_after.get(k)
    }
    global _LAST_CONFIG_SNAPSHOT
    if diffs:
        logger.info("configure_normalizer: config diff: %s", diffs)
    elif _LAST_CONFIG_SNAPSHOT is not None:
        logger.info("configure_normalizer: no changes")
    _LAST_CONFIG_SNAPSHOT = snapshot_after


