def normalize_activity_value(
    value: Any,
    units: Any,
    *,
    activity_type: str | None = None,
    temperature_c: float | None = None,
    raise_on_error: bool = False,
) -> ActivityValue:
    """Convert an activity measurement to nanomolar (nM) units.

    Returns an :class:`ActivityValue` NamedTuple.  Backward-compatible
    with the v2.0.0 2-tuple return contract::

        val, unit = normalize_activity_value(1.5, "uM")
        # val == 1500.0, unit == "nM"

    Supported unit conversions (case-insensitive — DESIGN-5):

    ===========  ===========  ===========
    Input        Multiplier   Output unit
    ===========  ===========  ===========
    ``pM``       × 1e-3       ``nM``
    ``nM``       × 1.0        ``nM``
    ``uM``       × 1e3        ``nM``
    ``µM``       × 1e3        ``nM``
    ``mM``       × 1e6        ``nM``
    ``M``        × 1e9        ``nM``
    ``mol/L``    × 1e9        ``nM``
    ``%``        None         original (not convertible)
    ===========  ===========  ===========

    Censor operators (">100", "<1", "~50") are detected and the
    resulting :class:`ActivityValue` has ``censored=True`` and
    ``censor_direction`` set (SCI-10).

    Parameters
    ----------
    value : int, float, str, or None
        The numeric activity measurement.  Strings with censor prefixes
        (">", "<", "~") are parsed.
    units : str or None
        The unit string.  None / non-string logs a WARNING (DQ-13).
    activity_type : str, optional
        One of :data:`_ALLOWED_ACTIVITY_TYPES` (IC50, Ki, Kd, EC50, ...).
        If omitted, a WARNING is logged (SCI-11) because comparing across
        activity types without context is scientifically wrong.
    temperature_c : float, optional
        Temperature in Celsius (relevant for Ki; SCI-20).  Validated
        against [0, 100].
    raise_on_error : bool
        If True, raise on invalid input.  If False (default), return an
        ActivityValue with ``value=None``.

    Returns
    -------
    ActivityValue
        NamedTuple with ``value``, ``unit``, ``original_value``,
        ``original_unit``, ``conversion_factor``, ``censored``,
        ``censor_direction``, ``activity_type``, ``temperature_c``,
        ``warnings``.

    Examples
    --------
    >>> r = normalize_activity_value(1.5, "uM")
    >>> r.value, r.unit
    (1500.0, 'nM')
    >>> val, unit = r  # backward-compat 2-tuple unpacking
    >>> val, unit
    (1500.0, 'nM')
    >>> r = normalize_activity_value(">100", "uM")
    >>> r.value, r.censored, r.censor_direction
    (100000.0, True, '>')
    """
    warnings_tuple: list[str] = []
    original_value = value
    original_unit = units if isinstance(units, str) else None

    # [SCI-11] Activity type validation.
    # v16 ROOT FIX (SW-4): if the upstream caller passes the STRING
    # "None" (a common pandas/CSV artifact when a missing value is
    # stringified), coerce it to Python None so it is treated as
    # "missing" rather than as a (now-invalid) activity type.
    if isinstance(activity_type, str) and activity_type.strip() == "None":
        logger.warning(
            "normalize_activity_value: activity_type was the string 'None' "
            "— coercing to Python None (missing). See SW-4 root fix."
        )
        warnings_tuple.append("activity_type_string_none_coerced")
        activity_type = None

    if activity_type is not None:
        if activity_type not in _ALLOWED_ACTIVITY_TYPES:
            logger.warning(
                "normalize_activity_value: activity_type %r not in allowed "
                "set %s — preserving but flagging",
                activity_type,
                sorted(_ALLOWED_ACTIVITY_TYPES),
            )
            warnings_tuple.append(f"unknown_activity_type:{activity_type}")
    else:
        # [SCI-11] Warn when activity_type is omitted.
        logger.warning(
            "normalize_activity_value: activity_type not provided — "
            "downstream consumers MUST segment by activity_type before "
            "ranking (IC50 ≠ Ki ≠ Kd)"
        )
        warnings_tuple.append("missing_activity_type")

    # [SCI-20] Temperature validation.
    if temperature_c is not None:
        try:
            temperature_c = float(temperature_c)
            if not (0.0 <= temperature_c <= 100.0):
                logger.warning(
                    "normalize_activity_value: temperature_c %s out of "
                    "range [0, 100] — preserving but flagging",
                    temperature_c,
                )
                warnings_tuple.append(f"temperature_out_of_range:{temperature_c}")
        except (ValueError, TypeError):
            logger.warning(
                "normalize_activity_value: temperature_c %r is not numeric "
                "— ignoring",
                temperature_c,
            )
            temperature_c = None

    # [DQ-13] Handle None / non-string units.
    if units is None:
        logger.warning(
            "normalize_activity_value: units is None — caller may have "
            "intended 'nM' (returning value=None, unit='')"
        )
        warnings_tuple.append("units_is_none")
        return ActivityValue(
            value=None,
            unit="",
            original_value=original_value,
            original_unit=original_unit,
            censored=False,
            activity_type=activity_type,
            temperature_c=temperature_c,
            warnings=tuple(warnings_tuple),
        )
    if not isinstance(units, str):
        logger.debug(
            "normalize_activity_value: units is %s, not str — coercing",
            type(units).__name__,
        )
        try:
            units = str(units)
        except Exception:
            units = ""
    if units == "":
        logger.debug(
            "normalize_activity_value: empty units — returning value=None"
        )
        warnings_tuple.append("units_empty")
        return ActivityValue(
            value=None,
            unit="",
            original_value=original_value,
            original_unit=original_unit,
            censored=False,
            activity_type=activity_type,
            temperature_c=temperature_c,
            warnings=tuple(warnings_tuple),
        )

    # Handle None value.
    if value is None:
        return ActivityValue(
            value=None,
            unit=units,
            original_value=original_value,
            original_unit=original_unit,
            censored=False,
            activity_type=activity_type,
            temperature_c=temperature_c,
            warnings=tuple(warnings_tuple),
        )

    # [SCI-10] Censor prefix detection.
    censored = False
    censor_direction: str | None = None
    numeric_value: float | None = None

    if isinstance(value, str):
        m = _CENSOR_PATTERN.match(value)
        if m:
            censor_direction = m.group(1)
            try:
                numeric_value = float(m.group(2))
                censored = True
            except (ValueError, TypeError):
                numeric_value = None
        else:
            try:
                numeric_value = float(value)
            except (ValueError, TypeError):
                # [DESIGN-8] Return value=None on parse failure.
                logger.warning(
                    "normalize_activity_value: non-numeric value %r with "
                    "units %r",
                    value,
                    units,
                )
                warnings_tuple.append("non_numeric_value")
                return ActivityValue(
                    value=None,
                    unit=units,
                    original_value=original_value,
                    original_unit=original_unit,
                    censored=False,
                    activity_type=activity_type,
                    temperature_c=temperature_c,
                    warnings=tuple(warnings_tuple),
                )
    else:
        # [REL-15] Guard against datetime / NaT / non-numeric types.
        try:
            # [CODE-3] bool is not a valid activity value.
            if isinstance(value, bool):
                logger.warning(
                    "normalize_activity_value: bool value %r — treating as None",
                    value,
                )
                warnings_tuple.append("bool_value")
                return ActivityValue(
                    value=None,
                    unit=units,
                    original_value=original_value,
                    original_unit=original_unit,
                    censored=False,
                    activity_type=activity_type,
                    temperature_c=temperature_c,
                    warnings=tuple(warnings_tuple),
                )
            numeric_value = float(value)
        except (ValueError, TypeError):
            # [DESIGN-8, REL-15] Return value=None on parse failure.
            logger.warning(
                "normalize_activity_value: non-numeric value %r (type %s) "
                "with units %r",
                value,
                type(value).__name__,
                units,
            )
            warnings_tuple.append("non_numeric_value")
            return ActivityValue(
                value=None,
                unit=units,
                original_value=original_value,
                original_unit=original_unit,
                censored=False,
                activity_type=activity_type,
                temperature_c=temperature_c,
                warnings=tuple(warnings_tuple),
            )

    if numeric_value is None:
        return ActivityValue(
            value=None,
            unit=units,
            original_value=original_value,
            original_unit=original_unit,
            censored=censored,
            censor_direction=censor_direction,
            activity_type=activity_type,
            temperature_c=temperature_c,
            warnings=tuple(warnings_tuple),
        )

    # [REL-9] Infinity / NaN guard.
    if not math.isfinite(numeric_value):
        logger.warning(
            "normalize_activity_value: value %r is not finite (inf/NaN) — "
            "returning value=None",
            numeric_value,
        )
        warnings_tuple.append("non_finite_value")
        return ActivityValue(
            value=None,
            unit=units,
            original_value=original_value,
            original_unit=original_unit,
            censored=censored,
            censor_direction=censor_direction,
            activity_type=activity_type,
            temperature_c=temperature_c,
            warnings=tuple(warnings_tuple),
        )

    # [SCI-9] Negative concentration guard.
    # v16 ROOT FIX (SW-6): the previous code returned ``censored=True,
    # censor_direction=None`` for negative values. "Censored" in
    # pharmacology means "we know the true value is >X or <X but
    # couldn't measure precisely" — e.g. ``">30 nM"`` or ``"<0.1 nM"``.
    # A NEGATIVE concentration is not censored, it is CORRUPT —
    # concentrations cannot be negative in any unit system.
    # Marking it censored caused downstream TransE training to
    # include corrupt negatives in the "censored but valid" filter,
    # biasing the model toward impossibly-potent edges.
    # The fix: mark as ``is_corrupt=True`` (new flag) and
    # ``censored=False`` so corrupt values are excluded from BOTH
    # the "valid measurement" set AND the "censored measurement" set.
    if numeric_value < 0:
        logger.warning(
            "normalize_activity_value: negative value %r — returning "
            "value=None, is_corrupt=True (impossible concentration, "
            "NOT censored — see SW-6 root fix)",
            numeric_value,
        )
        warnings_tuple.append("negative_value_corrupt")
        return ActivityValue(
            value=None,
            unit=units,
            original_value=original_value,
            original_unit=original_unit,
            censored=False,
            censor_direction=None,
            activity_type=activity_type,
            temperature_c=temperature_c,
            warnings=tuple(warnings_tuple),
            is_corrupt=True,
        )

    # [DQ-13] Log INFO when value == 0 (suspicious but not impossible).
    if numeric_value == 0:
        logger.info(
            "normalize_activity_value: value is 0 — suspicious but may be "
            "legitimate ('no activity')"
        )

    # audit-2025 ROOT FIX (issue 16): log-scale activity types
    # (pKi / pIC50 / pEC50 / pKd) are NEGATIVE-LOG10 of the linear
    # molar concentration. They MUST NOT be multiplied by the linear
    # unit conversion factor — that would produce nonsense like
    # "8.5 nM" from a pKi of 8.5 (which actually represents ~3.16 nM).
    # The audit's recommended fix (option b): mark the value with a
    # ``unit`` field equal to the activity_type ("pKi", "pIC50", ...)
    # so downstream consumers can identify and convert/exclude as
    # needed. The original_value/original_unit preserve the source
    # data for reproducibility.
    if activity_type in _LOG_SCALE_ACTIVITY_TYPES:
        logger.info(
            "normalize_activity_value: activity_type %r is log-scale — "
            "preserving value %r verbatim with unit=%r (downstream "
            "consumers MUST convert via nM = 10 ** (9 - %s) before "
            "aggregating with linear nM values)",
            activity_type,
            numeric_value,
            activity_type,
            activity_type,
        )
        warnings_tuple.append(f"log_scale_activity_type:{activity_type}")
        return ActivityValue(
            value=numeric_value,
            unit=activity_type,  # mark as log-scale, NOT "nM"
            original_value=original_value,
            original_unit=original_unit,
            conversion_factor=None,  # no linear conversion applied
            censored=censored,
            censor_direction=censor_direction,
            activity_type=activity_type,
            temperature_c=temperature_c,
            warnings=tuple(warnings_tuple),
            is_log_scale=True,  # v43 P1-035: explicit flag for downstream
        )

    # [DESIGN-5] Case-insensitive unit lookup.
    normalized_units = units.strip()
    factor = _UNIT_CONVERSIONS_CF.get(normalized_units.casefold())

    if factor is None:
        # [DESIGN-6] Recognized but non-convertible units (e.g., "%").
        if normalized_units == "%":
            logger.info(
                "normalize_activity_value: unit '%%' is not convertible to "
                "nM — returning value unchanged"
            )
            warnings_tuple.append("percent_not_convertible")
            return ActivityValue(
                value=numeric_value,
                unit=normalized_units,
                original_value=original_value,
                original_unit=original_unit,
                censored=censored,
                censor_direction=censor_direction,
                activity_type=activity_type,
                temperature_c=temperature_c,
                warnings=tuple(warnings_tuple),
            )
        # Unknown unit — return as-is.
        logger.debug(
            "normalize_activity_value: unrecognised unit %r — returning "
            "value unchanged",
            normalized_units,
        )
        warnings_tuple.append(f"unknown_unit:{normalized_units}")
        return ActivityValue(
            value=numeric_value,
            unit=normalized_units,
            original_value=original_value,
            original_unit=original_unit,
            censored=censored,
            censor_direction=censor_direction,
            activity_type=activity_type,
            temperature_c=temperature_c,
            warnings=tuple(warnings_tuple),
        )

    # [DESIGN-6, CODE-9] Convert to nM.
    converted = numeric_value * factor

    # [DQ-7] Range validation post-conversion.
    if abs(converted) > _ACTIVITY_VALUE_MAX:
        # audit-2025 ROOT FIX (issue 17): the previous code set
        # ``censored=True`` AND ``censor_direction=">"`` BUT left
        # ``value=converted`` (the raw non-physical number, e.g.
        # 1.5e10 nM = 15 M). Downstream ML consumers that filter on
        # ``censored == True`` to exclude "out of measurable range"
        # rows would still pick up the raw value via the ``value``
        # field — silently corrupting the feature column. The audit's
        # recommended fix: set ``value=None`` when censored AND
        # out-of-range, keeping the original (raw) value in
        # ``original_value`` so the data is preserved for forensics
        # but excluded from aggregations.
        logger.warning(
            "normalize_activity_value: converted value %s exceeds cap %s "
            "— marking as censored (effectively '> measurable') and "
            "setting value=None to exclude from nM aggregation "
            "(original_value preserved)",
            converted,
            _ACTIVITY_VALUE_MAX,
        )
        warnings_tuple.append(f"value_exceeds_cap:{converted}")
        return ActivityValue(
            value=None,  # audit-2025: was `converted` (non-physical)
            unit="nM",
            original_value=original_value,
            original_unit=original_unit,
            conversion_factor=factor,
            censored=True,
            censor_direction=">",
            activity_type=activity_type,
            temperature_c=temperature_c,
            warnings=tuple(warnings_tuple),
        )

    # [IDEM-12] Round to significant figures.
    converted = _round_sig(converted, _ACTIVITY_VALUE_SIG_FIGS)

    # [LOG-3] Metric.
    with _METRICS_LOCK:
        _METRICS["activity_values_normalized"] += 1

    # [LINEAGE-14] Audit log.
    _audit_log_local(
        "normalize_activity_value",
        {
            "original_value": str(original_value),
            "original_unit": original_unit,
            "converted_value": converted,
            "converted_unit": "nM",
            "factor": factor,
            "censored": censored,
            "activity_type": activity_type,
        },
    )

    return ActivityValue(
        value=converted,
        unit="nM",
        original_value=original_value,
        original_unit=original_unit,
        conversion_factor=factor,
        censored=censored,
        censor_direction=censor_direction,
        activity_type=activity_type,
        temperature_c=temperature_c,
        warnings=tuple(warnings_tuple),
    )


def normalize_activity_values(
    values: Iterable[Any],
    units: Union[Iterable[str], str],
    *,
    activity_type: str | None = None,
    temperature_c: float | None = None,
) -> list[ActivityValue]:
    """Batch activity value normalization (PERF-8).

    Parameters
    ----------
    values : iterable
        Activity values.
    units : iterable of str OR str
        If a string, broadcast to all values.  If an iterable, must be the
        same length as values.
    activity_type : str, optional
        Forwarded to :func:`normalize_activity_value`.
    temperature_c : float, optional
        Forwarded to :func:`normalize_activity_value`.

    Returns
    -------
    list[ActivityValue]
        Normalized activity values.
    """
    values_list = list(values)
    if isinstance(units, str):
        units_list = [units] * len(values_list)
    else:
        units_list = list(units)
    if len(units_list) != len(values_list):
        raise ValueError(
            f"normalize_activity_values: values ({len(values_list)}) and "
            f"units ({len(units_list)}) must have the same length"
        )
    return [
        normalize_activity_value(v, u, activity_type=activity_type, temperature_c=temperature_c)
        for v, u in zip(values_list, units_list)
    ]


