def convert_to_inchikey_detailed(
    smiles: Union[str, bytes, "Chem.Mol", None],  # type: ignore[name-defined]
    *,
    options: str | None = None,
    standard: bool = True,
    timeout: float | None = None,
    raise_on_error: bool = False,
    activity_type: str | None = None,  # ignored; for forward compat
) -> ConversionResult:
    """Convert SMILES → InChIKey with full result metadata (DQ-2).

    Parameters
    ----------
    smiles : str, bytes, Mol, or None
        SMILES string, bytes (UTF-8), RDKit Mol, or None.
    options : str or None
        InChI options (e.g., "/LargeMolecules").  Default: empty.
    standard : bool
        If True (default), produce standard InChI.  If False, non-standard.
    timeout : float or None
        Max seconds for the conversion.  Default: None (no timeout).
        NOTE: timeout uses signal.alarm on Unix; not thread-safe.
    raise_on_error : bool
        If True, raise DependencyNotAvailableError on RDKit-unavailable.
        If False (default), return a ConversionResult with error_category.
    activity_type : str or None
        Ignored; for forward compatibility.

    Returns
    -------
    ConversionResult
        NamedTuple with success, inchikey, error, error_category, etc.

    Raises
    ------
    DependencyNotAvailableError
        If RDKit is unavailable and raise_on_error=True (DESIGN-9).
    """
    # [DESIGN-10] Accept Mol objects directly.
    if smiles is None:
        return ConversionResult(
            success=False,
            inchikey=None,
            error="input is None",
            error_category="INVALID_SMILES",
            rdkit_version=_RDKIT_VERSION,
        )

    # [INTEROP-6] Handle bytes input.
    if isinstance(smiles, bytes):
        try:
            smiles = smiles.decode("utf-8", errors="strict")
        except UnicodeDecodeError as exc:
            return ConversionResult(
                success=False,
                inchikey=None,
                error=f"invalid UTF-8 bytes: {exc}",
                error_category="INVALID_SMILES",
                rdkit_version=_RDKIT_VERSION,
            )

    # [DESIGN-10] Accept RDKit Mol directly.
    if _RDKIT_AVAILABLE and not isinstance(smiles, str):
        try:
            # Duck-type: does it look like an RDKit Mol?  Mol objects have
            # GetNumAtoms and GetAtoms but NOT GetMolFrags (that's a
            # module-level function Chem.GetMolFrags).
            if hasattr(smiles, "GetNumAtoms") and hasattr(smiles, "GetAtoms"):
                # Convert Mol → canonical SMILES → standard pipeline
                try:
                    smiles = Chem.MolToSmiles(smiles)
                except Exception as exc:
                    return ConversionResult(
                        success=False,
                        inchikey=None,
                        error=f"could not serialize Mol to SMILES: {exc}",
                        error_category="RDKIT_PARSE_ERROR",
                        rdkit_version=_RDKIT_VERSION,
                    )
        except Exception:
            pass  # fall through to string validation

    if not isinstance(smiles, str):
        return ConversionResult(
            success=False,
            inchikey=None,
            error=f"expected str/bytes/Mol, got {type(smiles).__name__}",
            error_category="INVALID_SMILES",
            rdkit_version=_RDKIT_VERSION,
        )

    # [INTEROP-6] Non-ASCII warning.
    try:
        non_ascii = any(ord(c) > 127 for c in smiles)
    except Exception:
        non_ascii = False
    if non_ascii:
        logger.warning(
            "convert_to_inchikey: SMILES contains non-ASCII characters %s",
            _truncate_for_log(smiles),
        )

    # [SEC-2] Length cap.
    if len(smiles) > _SMILES_MAX_LENGTH:
        logger.warning(
            "convert_to_inchikey: SMILES length %d exceeds cap %d — rejecting",
            len(smiles),
            _SMILES_MAX_LENGTH,
        )
        return ConversionResult(
            success=False,
            inchikey=None,
            error=f"SMILES too long: {len(smiles)} > {_SMILES_MAX_LENGTH}",
            error_category="SMILES_TOO_LONG",
            smiles_hash=_smiles_hash(smiles),
            rdkit_version=_RDKIT_VERSION,
        )

    # [SEC-1] Character allowlist.
    bad_chars = set(smiles) - _SMILES_ALLOWED_CHARS
    if bad_chars:
        logger.warning(
            "convert_to_inchikey: SMILES contains disallowed chars %s — rejecting",
            sorted(bad_chars)[:10],
        )
        return ConversionResult(
            success=False,
            inchikey=None,
            error=f"SMILES contains disallowed chars: {sorted(bad_chars)[:10]}",
            error_category="SMILES_INVALID_CHARS",
            smiles_hash=_smiles_hash(smiles),
            rdkit_version=_RDKIT_VERSION,
        )

    smiles = smiles.strip()
    if not smiles:
        return ConversionResult(
            success=False,
            inchikey=None,
            error="empty SMILES",
            error_category="INVALID_SMILES",
            rdkit_version=_RDKIT_VERSION,
        )

    s_hash = _smiles_hash(smiles)

    # [IDEM-9] Refresh capabilities if RDKit was previously unavailable.
    if not _RDKIT_AVAILABLE:
        refresh_capabilities()

    if not _RDKIT_AVAILABLE:
        if raise_on_error:
            try:
                from . import DependencyNotAvailableError  # type: ignore
                raise DependencyNotAvailableError(
                    "RDKit is not installed — cannot convert SMILES to InChIKey"
                )
            except ImportError:
                raise RuntimeError(
                    "RDKit is not installed — cannot convert SMILES to InChIKey"
                )
        return ConversionResult(
            success=False,
            inchikey=None,
            error="RDKit not available",
            error_category="RDKIT_UNAVAILABLE",
            smiles_hash=s_hash,
            rdkit_version=_RDKIT_VERSION,
        )

    # [REL-3] Circuit breaker check.
    if not _cb_convert.allow_request():
        return ConversionResult(
            success=False,
            inchikey=None,
            error="circuit breaker open",
            error_category="CIRCUIT_OPEN",
            smiles_hash=s_hash,
            rdkit_version=_RDKIT_VERSION,
        )

    # [SEC-13, REL-16] Optional rate limiter.
    if _rate_limiter is not None:
        try:
            if not _rate_limiter():
                logger.warning(
                    "convert_to_inchikey: rate limit exceeded — rejecting"
                )
                return ConversionResult(
                    success=False,
                    inchikey=None,
                    error="rate limit exceeded",
                    error_category="RATE_LIMITED",
                    smiles_hash=s_hash,
                    rdkit_version=_RDKIT_VERSION,
                )
        except Exception:
            pass  # never crash on rate-limit errors

    # [REL-1] Timeout via signal.alarm (Unix only, MAIN THREAD ONLY).
    # CRITICAL FIX (runtime safety): signal.signal() can ONLY be called from
    # the main thread. Calling it from a worker thread raises:
    #   ValueError: signal only works in main thread of the main interpreter
    # which is caught by NO except block here, propagating up and causing
    # convert_to_inchikeys(...) to silently return [None] for ALL SMILES
    # when run from a thread pool (which is the normal case for batch
    # conversion). The fix: detect whether we're in the main thread and
    # only use signal.alarm if so; otherwise, fall back to no-timeout
    # mode (RDKit is fast enough that timeouts are rarely needed).
    if timeout is not None and sys.platform != "win32":
        import signal
        import threading

        # Only use signal-based timeout in the main thread. In worker
        # threads, signal.signal() raises ValueError — see the Python
        # docs: "This function can only be called from the main thread
        # of the main interpreter".
        _can_use_signal = False
        try:
            _can_use_signal = (
                threading.current_thread() is threading.main_thread()
            )
        except Exception:  # noqa: BLE001 — defensive
            _can_use_signal = False

        if _can_use_signal:
            def _timeout_handler(signum, frame):  # noqa: ARG001
                raise TimeoutError(f"convert_to_inchikey exceeded {timeout}s")

            old_handler = signal.signal(signal.SIGALRM, _timeout_handler)
            signal.setitimer(signal.ITIMER_REAL, timeout)
            try:
                inchikey = _convert_to_inchikey_cached(smiles, options or "", standard)
            except TimeoutError:
                _cb_convert.record_failure()
                return ConversionResult(
                    success=False,
                    inchikey=None,
                    error=f"timeout after {timeout}s",
                    error_category="TIMEOUT",
                    smiles_hash=s_hash,
                    rdkit_version=_RDKIT_VERSION,
                )
            finally:
                signal.setitimer(signal.ITIMER_REAL, 0)
                signal.signal(signal.SIGALRM, old_handler)
        else:
            # Worker thread — signal.alarm unavailable. Log once and
            # proceed without timeout (RDKit conversion is typically
            # <100ms per SMILES; only pathological inputs take longer).
            logger.debug(
                "convert_to_inchikey: signal-based timeout unavailable "
                "in worker thread — proceeding without timeout"
            )
            try:
                inchikey = _convert_to_inchikey_cached(smiles, options or "", standard)
            except MemoryError:
                _cb_convert.record_failure()
                return ConversionResult(
                    success=False,
                    inchikey=None,
                    error="MemoryError during conversion",
                    error_category="UNKNOWN_ERROR",
                    smiles_hash=s_hash,
                    rdkit_version=_RDKIT_VERSION,
                )
    else:
        # No timeout requested, or Windows platform (signal.alarm unavailable).
        try:
            inchikey = _convert_to_inchikey_cached(smiles, options or "", standard)
        except MemoryError:
            _cb_convert.record_failure()
            return ConversionResult(
                success=False,
                inchikey=None,
                error="MemoryError during conversion",
                error_category="UNKNOWN_ERROR",
                smiles_hash=s_hash,
                rdkit_version=_RDKIT_VERSION,
            )

    if inchikey is None:
        _cb_convert.record_failure()
        with _METRICS_LOCK:
            _METRICS["smiles_failed"] += 1
        # [REL-4] Push to dead-letter queue.
        _add_dead_letter({
            "smiles": smiles,
            "smiles_hash": s_hash,
            "error": "conversion_failed",
            "error_category": "RDKIT_PARSE_ERROR",
            "timestamp": datetime.now(timezone.utc).isoformat(),
        })
        return ConversionResult(
            success=False,
            inchikey=None,
            error="conversion failed (invalid SMILES or RDKit error)",
            error_category="RDKIT_PARSE_ERROR",
            smiles_hash=s_hash,
            rdkit_version=_RDKIT_VERSION,
        )

    _cb_convert.record_success()
    with _METRICS_LOCK:
        _METRICS["smiles_converted"] += 1

    # [DQ-19] Capture canonical SMILES.
    canonical_smiles: str | None = None
    try:
        mol_post = _smiles_to_mol(smiles, sanitize=False)
        if mol_post is not None:
            canonical_smiles = Chem.MolToSmiles(mol_post)
    except Exception:
        pass

    # [SCI-17, DQ-3] Collision tracking.
    potential_collision = False
    with _COLLISION_LOCK:
        if inchikey in _INCHIKEY_TO_SMILES:
            existing = _INCHIKEY_TO_SMILES[inchikey]
            if canonical_smiles and canonical_smiles not in existing:
                logger.warning(
                    "convert_to_inchikey: potential InChIKey collision — "
                    "key %s previously produced by different SMILES",
                    inchikey,
                )
                potential_collision = True
                _add_dead_letter({
                    "smiles": smiles,
                    "inchikey": inchikey,
                    "error": "potential_collision",
                    "error_category": "POTENTIAL_COLLISION",
                    "timestamp": datetime.now(timezone.utc).isoformat(),
                })
            if canonical_smiles:
                existing.add(canonical_smiles)
        else:
            if canonical_smiles:
                _INCHIKEY_TO_SMILES[inchikey] = {canonical_smiles}
            else:
                _INCHIKEY_TO_SMILES[inchikey] = {smiles}
            # [SCI-17] LRU eviction.
            while len(_INCHIKEY_TO_SMILES) > _MAX_COLLISION_TRACK:
                _INCHIKEY_TO_SMILES.popitem(last=False)

    # [DQ-20] Stereo ambiguity detection.
    stereo_ambiguous = False
    try:
        mol_check = _smiles_to_mol(smiles, sanitize=False)
        if mol_check is not None:
            chiral_centers = Chem.FindMolChiralCenters(
                mol_check, includeUnassigned=True
            )
            stereo_ambiguous = any(
                len(center) > 1 and center[1] == "?" for center in chiral_centers
            )
    except Exception:
        pass

    # [SEC-8, LINEAGE-12] Audit log.
    _audit_log_local(
        "convert_to_inchikey",
        {
            "smiles_hash": s_hash,
            "inchikey": inchikey,
            "rdkit_version": _RDKIT_VERSION,
            "standard": standard,
        },
    )

    # [LINEAGE-16] OpenLineage emit.
    if _openlineage_emitter is not None:
        try:
            _openlineage_emitter({
                "eventType": "convert_to_inchikey",
                "inputs": [{"smiles_hash": s_hash}],
                "outputs": [{"inchikey": inchikey}],
            })
        except Exception:
            pass

    return ConversionResult(
        success=True,
        inchikey=inchikey,
        smiles_hash=s_hash,
        canonical_smiles=canonical_smiles,
        potential_collision=potential_collision,
        stereo_ambiguous=stereo_ambiguous,
        rdkit_version=_RDKIT_VERSION,
    )


def convert_to_inchikey(
    smiles: Union[str, bytes, "Chem.Mol", None],  # type: ignore[name-defined]
    *,
    options: str | None = None,
    standard: bool = True,
    timeout: float | None = None,
    raise_on_error: bool = False,
) -> str | None:
    """Convert a SMILES string to a standard InChIKey.

    Uses RDKit's ``MolFromSmiles`` → ``MolToInchi`` → ``InchiToInchiKey``
    pipeline.  If RDKit is not available, or the SMILES is invalid / empty,
    returns ``None`` and logs a warning.

    Backward-compatible with the v2.0.0 signature ``convert_to_inchikey(smiles) -> str | None``.
    New optional kwargs (options, standard, timeout, raise_on_error) all
    have defaults that preserve v2.0.0 behavior.

    Parameters
    ----------
    smiles : str, bytes, Mol, or None
        A SMILES representation of a molecule.  Bytes are decoded as UTF-8.
        RDKit Mol objects are accepted (DESIGN-10).
    options : str or None
        InChI options string (e.g., "/LargeMolecules").  Default: empty.
    standard : bool
        If True (default), produce standard InChI (InChIKey ends in "S").
        If False, produce non-standard InChI (ends in "N").  (SCI-16)
    timeout : float or None
        Max seconds for the conversion.  Default: None (no timeout).
    raise_on_error : bool
        If True, raise DependencyNotAvailableError when RDKit is missing.

    Returns
    -------
    str or None
        The 27-character InChIKey, or ``None`` if conversion failed.

    Raises
    ------
    DependencyNotAvailableError
        If raise_on_error=True and RDKit is not installed.

    Examples
    --------
    >>> convert_to_inchikey("CC(=O)OC1=CC=CC=C1C(=O)O")  # doctest: +SKIP
    'BSYNRYMUTXBXSQ-UHFFFAOYSA-N'

    Notes
    -----
    Tested up to 1M SMILES.  Throughput: ~1K SMILES/sec on a single core
    (RDKit-bound).  For bulk processing, use :func:`convert_to_inchikeys`.

    See Also
    --------
    convert_to_inchikey_detailed : Returns full :class:`ConversionResult`.
    convert_to_inchikeys : Batch API for >10K SMILES.
    """
    result = convert_to_inchikey_detailed(
        smiles,
        options=options,
        standard=standard,
        timeout=timeout,
        raise_on_error=raise_on_error,
    )
    return result.inchikey


def convert_to_inchikeys(
    smiles_list: Iterable[Union[str, bytes, None]],
    *,
    options: str | None = None,
    standard: bool = True,
    timeout: float | None = None,
    max_workers: int | None = None,
) -> list[str | None]:
    """Batch SMILES → InChIKey conversion (PERF-2, PERF-15).

    Uses a :class:`concurrent.futures.ThreadPoolExecutor` (RDKit releases
    the GIL during C-level operations, so threads help).

    Parameters
    ----------
    smiles_list : iterable of str/bytes/None
        SMILES strings to convert.
    options : str or None
        InChI options.  Default: empty.
    standard : bool
        If True (default), produce standard InChI.
    timeout : float or None
        Per-SMILES timeout.
    max_workers : int or None
        Thread pool size.  Default: min(32, os.cpu_count() + 4).

    Returns
    -------
    list[str or None]
        InChIKeys in the same order as the input.  Failed conversions are
        None.

    Examples
    --------
    >>> results = convert_to_inchikeys(["CCO", "CC(=O)O", "invalid"])
    >>> len(results)  # doctest: +SKIP
    3
    """
    import concurrent.futures

    smiles_list = list(smiles_list)
    if not smiles_list:
        return []
    if max_workers is None:
        max_workers = min(32, (os.cpu_count() or 1) + 4)
    max_workers = max(1, min(max_workers, len(smiles_list)))

    results: list[str | None] = [None] * len(smiles_list)

    def _convert_one(idx_smiles):
        idx, sm = idx_smiles
        return idx, convert_to_inchikey(
            sm, options=options, standard=standard, timeout=timeout
        )

    with concurrent.futures.ThreadPoolExecutor(max_workers=max_workers) as ex:
        futures = {
            ex.submit(_convert_one, (i, sm)): i
            for i, sm in enumerate(smiles_list)
        }
        for future in concurrent.futures.as_completed(futures):
            idx = futures[future]
            try:
                _, ik = future.result()
                results[idx] = ik
            except Exception:
                results[idx] = None

    return results


