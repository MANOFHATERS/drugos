def validate_config() -> list[str]:
    """Return a list of config warnings (empty = healthy) (CFG-10).

    Returns
    -------
    list[str]
        Human-readable warning strings.
    """
    warnings_list: list[str] = []
    if not (0.0 <= _FUZZY_THRESHOLD <= 1.0):
        warnings_list.append(f"fuzzy_threshold out of [0,1]: {_FUZZY_THRESHOLD}")
    if not ALLOWED_TYPES:
        warnings_list.append("ALLOWED_TYPES is empty")
    if _DEFAULT_DRUG_TYPE not in ALLOWED_TYPES:
        warnings_list.append(
            f"_DEFAULT_DRUG_TYPE {_DEFAULT_DRUG_TYPE!r} not in ALLOWED_TYPES"
        )
    for k, v in _UNIT_CONVERSIONS.items():
        if not isinstance(v, (int, float)) or not math.isfinite(float(v)):
            warnings_list.append(f"_UNIT_CONVERSIONS['{k}'] is not finite: {v}")
    return warnings_list


