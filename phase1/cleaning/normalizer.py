def watch_config(path: str, interval_sec: float = 60.0) -> None:
    """Start a daemon thread that watches a config file for changes (CFG-16).

    v22 ROOT FIX (audit section 6 finding 7 / section 9 — misleading
    "stub" docstring): the previous docstring said "This is a stub — it
    starts the thread but the thread only logs. Full hot-reload is out
    of scope." That was FALSE — the function body actually implements
    real mtime-based watching and calls ``load_config(path)`` on
    change. The misleading docstring led operators to believe hot-reload
    did not work. Update the docstring to reflect the real behavior.

    Behavior:
      - Starts a daemon thread that polls the file's mtime every
        ``interval_sec`` seconds.
      - On mtime change: calls ``load_config(path)`` to reload the
        normalizer's runtime config.
      - Errors during stat/reload are logged at DEBUG level (the
        watcher keeps running).
      - Thread is daemon — exits when the main process exits.

    Parameters
    ----------
    path : str
        Config file path to watch.
    interval_sec : float
        Check interval in seconds.  Default: 60.
    """
    global _config_watcher_thread

    def _watch():
        last_mtime = 0.0
        try:
            last_mtime = Path(path).stat().st_mtime
        except Exception:
            pass
        while True:
            time.sleep(interval_sec)
            try:
                mtime = Path(path).stat().st_mtime
                if mtime > last_mtime:
                    logger.info("watch_config: %s changed — reloading", path)
                    load_config(path)
                    last_mtime = mtime
            except Exception as exc:
                logger.debug("watch_config: %s", exc)

    if _config_watcher_thread is None or not _config_watcher_thread.is_alive():
        _config_watcher_thread = threading.Thread(
            target=_watch, daemon=True, name="normalizer-config-watcher"
        )
        _config_watcher_thread.start()
        logger.info("watch_config: started daemon thread watching %s", path)


def requires_api_version(min_version: str) -> None:
    """Raise RuntimeError if __version__ < min_version (INTEROP-8).

    Parameters
    ----------
    min_version : str
        Minimum required version (semver).

    Raises
    ------
    RuntimeError
        If the current version is less than min_version.
    """
    def _parse(v: str) -> tuple[int, int, int]:
        parts = v.split(".")
        # Pad to length 3 so partial versions (e.g. "1" or "1.2") compare
        # correctly against full semver strings. Without padding,
        # ``_parse("1")`` returns ``(1,)`` and ``_parse("1.0")`` returns
        # ``(1, 0)`` — Python's tuple comparison treats a shorter prefix
        # as LESS than a longer prefix when the leading elements are
        # equal, so ``(1,) < (1, 0)`` is True and 1.0 would be wrongly
        # flagged as < 1 (v35 root fix).
        nums = [int(p) for p in parts[:3]]
        while len(nums) < 3:
            nums.append(0)
        return (nums[0], nums[1], nums[2])
    if _parse(__version__) < _parse(min_version):
        raise RuntimeError(
            f"requires normalizer >= {min_version}, got {__version__}"
        )


def is_backfill_needed(
    old_rule_version: str,
    new_rule_version: str,
) -> bool:
    """Return True if old < new (semver compare) (IDEM-8).

    Parameters
    ----------
    old_rule_version : str
        The rule_version stored in a record's _provenance.
    new_rule_version : str
        The current _RULE_VERSION.

    Returns
    -------
    bool
        True if backfill is needed.
    """
    def _parse(v: str) -> tuple[int, int, int]:
        # Strip non-numeric prefix (e.g., "rules_v3" → "3")
        digits = re.sub(r"[^0-9.]", "", v)
        parts = digits.split(".") if digits else ["0"]
        try:
            # Pad to length 3 (same reason as ``requires_api_version`` —
            # partial versions must compare correctly against full semver).
            nums = [int(p) for p in parts[:3]]
            while len(nums) < 3:
                nums.append(0)
            return (nums[0], nums[1], nums[2])
        except ValueError:
            return (0, 0, 0)
    return _parse(old_rule_version) < _parse(new_rule_version)


def sign_output(record: dict, signer_id: str) -> dict:
    """Add a minimal e-signature to a record's _provenance (COMP-8).

    v22 ROOT FIX (audit section 6 finding 7 / section 9 — misleading
    "stub" docstring): the previous docstring said "Add a minimal
    e-signature STUB" — but the function body actually adds real
    ``signed_by`` and ``signed_at`` (UTC ISO 8601) fields. The
    "stub" wording misled operators into believing no signing
    occurred. Update the docstring to reflect the real (minimal but
    functional) behavior.

    NOTE: This is a minimal e-signature — it records WHO signed and
    WHEN, but does NOT perform cryptographic signing (no HMAC, no
    PKI, no hash chain). For full FDA 21 CFR Part 11 compliance,
    operators must add a cryptographic layer on top. The function
    is intentionally minimal so it can run in any environment
    without crypto dependencies.

    Parameters
    ----------
    record : dict
        The record to sign (typically the output of standardize_drug_record).
    signer_id : str
        The signer's identifier (user ID, system ID, etc.).

    Returns
    -------
    dict
        The same record with _provenance["signed_by"] and ["signed_at"] set.
    """
    if not isinstance(record, dict):
        return record
    if "_provenance" not in record or not isinstance(record["_provenance"], dict):
        record["_provenance"] = {}
    record["_provenance"]["signed_by"] = signer_id
    record["_provenance"]["signed_at"] = datetime.now(timezone.utc).isoformat()
    return record


