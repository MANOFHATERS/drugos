def _convert_to_inchikey_uncached(
    smiles: str,
    options: str,
    standard: bool,
) -> str | None:
    """Actual SMILES → InChIKey conversion logic (uncached)."""
    global _RDKIT_INCHI_BROKEN  # [REL-6] set on first MolToInchi AttributeError
    # [SCI-3, SCI-16] Build InChI options.
    opts = options if options is not None else _INCHI_OPTIONS_DEFAULT
    # [SCI-4] Parse SMILES (with sanitize=False fallback).
    mol = _smiles_to_mol(smiles)
    if mol is None:
        return None

    # [SCI-13] Validate atom count, handle mixtures.
    try:
        n_atoms = mol.GetNumAtoms()
    except Exception:
        n_atoms = 0
    if n_atoms == 0:
        logger.warning(
            "convert_to_inchikey: SMILES %s parsed to empty molecule",
            _truncate_for_log(smiles),
        )
        return None
    if n_atoms > _MAX_SMILES_ATOMS:
        logger.warning(
            "convert_to_inchikey: SMILES %s has %d atoms (> %d) — "
            "attempting conversion but performance may degrade",
            _truncate_for_log(smiles),
            n_atoms,
            _MAX_SMILES_ATOMS,
        )

    # [SCI-13] Multi-component SMILES — use the largest fragment.
    try:
        frags = Chem.GetMolFrags(mol, asMols=True)
    except Exception:
        frags = (mol,)
    if len(frags) > 1:
        logger.info(
            "convert_to_inchikey: SMILES %s has %d fragments — "
            "using the largest",
            _truncate_for_log(smiles),
            len(frags),
        )
        try:
            mol = max(frags, key=lambda m: m.GetNumAtoms())
        except Exception:
            pass  # keep original

    # [SCI-5] Tautomer canonicalization (guarded — not all RDKit builds
    # include MolStandardize).
    try:
        from rdkit.Chem.MolStandardize import rdMolStandardize  # type: ignore
        enumerator = rdMolStandardize.TautomerEnumerator()
        canonical_mol = enumerator.Canonicalize(mol)
        if canonical_mol is not None:
            # [SCI-5] DEBUG log when tautomer canonicalization changes the
            # molecule (compare canonical SMILES before/after).
            try:
                before = Chem.MolToSmiles(mol)
                after = Chem.MolToSmiles(canonical_mol)
                if before != after:
                    logger.debug(
                        "convert_to_inchikey: tautomer canonicalization "
                        "changed molecule for SMILES %s",
                        _truncate_for_log(smiles),
                    )
            except Exception:
                pass
            mol = canonical_mol
    except ImportError:
        pass  # MolStandardize not available
    except Exception as exc:
        logger.debug(
            "convert_to_inchikey: tautomer canonicalization failed for "
            "SMILES %s: %s",
            _truncate_for_log(smiles),
            exc,
        )

    # [SCI-6, SCI-16] Stereo handling (preserve by default).
    if STEREO_POLICY == "ignore":
        try:
            Chem.RemoveStereochemistry(mol)
        except Exception:
            pass

    # [REL-6] Check if MolToInchi is broken.
    if _RDKIT_INCHI_BROKEN:
        return None

    # [SCI-3] MolToInchi with options.
    try:
        inchi_kwargs: dict[str, Any] = {}
        if opts:
            inchi_kwargs["options"] = opts
        # [SCI-16] Pass standard=standard through.
        try:
            inchi = MolToInchi(mol, standard=standard, **inchi_kwargs)
        except TypeError:
            # Older RDKit may not accept `standard` kwarg
            inchi = MolToInchi(mol, **inchi_kwargs)
    except AttributeError as exc:
        # [REL-6] MolToInchi itself is broken (rare but possible).
        if not _RDKIT_INCHI_BROKEN:
            _RDKIT_INCHI_BROKEN = True
            logger.critical(
                "convert_to_inchikey: MolToInchi is broken (%s) — "
                "all future conversions will short-circuit",
                exc,
            )
        return None
    except (
        Chem.AtomValenceException,
        Chem.KekulizeException,
        Chem.AtomKekulizeException,
        Chem.ValenceException,
        MemoryError,
    ) as exc:
        # [CODE-1] Specific RDKit errors — log and return None.
        logger.warning(
            "convert_to_inchikey: RDKit error converting SMILES %s: %s",
            _truncate_for_log(smiles),
            exc,
        )
        return None
    except Exception as exc:
        # [CODE-1] Unexpected error — log CRITICAL and re-raise as
        # CleaningError (if raise_on_error) or return None.
        logger.error(
            "convert_to_inchikey: unexpected error converting SMILES %s: %s",
            _truncate_for_log(smiles),
            exc,
            exc_info=True,
        )
        return None

    if inchi is None:
        logger.warning(
            "convert_to_inchikey: MolToInchi returned None for SMILES %s",
            _truncate_for_log(smiles),
        )
        return None

    # InchiToInchiKey
    try:
        inchikey = InchiToInchiKey(inchi)
    except Exception as exc:
        logger.warning(
            "convert_to_inchikey: InchiToInchiKey failed for SMILES %s: %s",
            _truncate_for_log(smiles),
            exc,
        )
        return None

    if inchikey is None:
        logger.warning(
            "convert_to_inchikey: InchiToInchiKey returned None for SMILES %s",
            _truncate_for_log(smiles),
        )
        return None

    # [DQ-18, SEC-14] Validate the output against _INCHIKEY_PATTERN.
    if not _INCHIKEY_PATTERN.match(inchikey):
        logger.error(
            "convert_to_inchikey: RDKit returned malformed InChIKey %s for "
            "SMILES %s — rejecting",
            _sanitize_string_local(inchikey),
            _truncate_for_log(smiles),
        )
        _add_dead_letter({
            "smiles": smiles,
            "inchikey": inchikey,
            "error": "malformed_inchikey",
            "error_category": "RDKIT_INCHIKEY_ERROR",
            "timestamp": datetime.now(timezone.utc).isoformat(),
        })
        return None

    # [SEC-14] Sanitize the returned string (defensive).
    inchikey = _sanitize_string_local(inchikey, max_length=64)

    return inchikey


