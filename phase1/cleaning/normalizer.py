def __getattr__(name: str) -> Any:
    """Dynamic module attribute lookup (PEP 562).

    Provides live-reading aliases for ``FUZZY_THRESHOLD`` and
    ``UNIT_CONVERSIONS`` so that ``configure_normalizer(fuzzy_threshold=0.9)``
    is immediately visible via ``normalizer.FUZZY_THRESHOLD``.

    Also delegates to ``cleaning.__init__.__getattr__`` for cross-package
    lazy-loaded names.
    """
    if name == "FUZZY_THRESHOLD":
        return _FUZZY_THRESHOLD
    if name == "UNIT_CONVERSIONS":
        return types.MappingProxyType(_UNIT_CONVERSIONS)
    if name == "ALLOWED_TYPES":
        # Return the live list (Constraint #7: existing tests expect a list).
        return ALLOWED_TYPES
