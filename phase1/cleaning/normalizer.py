def fuzzy_match_drug_type(raw_type: Any) -> str:
    """Map a free-text drug type to the closest allowed enum value (DESIGN-3).

    Public alias of :func:`_fuzzy_match_drug_type`.  Uses :mod:`rapidfuzz`
    when available; otherwise falls back to a case-insensitive exact match.

    Parameters
    ----------
    raw_type : Any
        Raw drug type string from the source data.  Non-string inputs
        are converted to "Unknown".

    Returns
    -------
    str
        The best-matching value from :data:`ALLOWED_TYPES`, or
        :data:`_DEFAULT_DRUG_TYPE` ("Unknown") if no match exceeds the
        fuzzy threshold.

    Notes
    -----
    Behavior is **deterministic** (IDEM-3, IDEM-5): ALLOWED_TYPES is sorted
    alphabetically before fuzzy matching, so ties resolve consistently
    regardless of the canonical list order.

    The default scorer is ``WRatio`` (Constraint #4: backward compat with
    v2.0.0 — the existing test contract that "small_mol" → "Small molecule"
    depends on WRatio's inflation).  Callers wanting stricter matching
    should call ``configure_normalizer(fuzzy_scorer="token_sort_ratio")``.

    See Also
    --------
    fuzzy_match_drug_types : Batch API.
    """
    return _fuzzy_match_drug_type(raw_type)


def _fuzzy_match_drug_type(raw_type: Any) -> str:
    """Internal implementation of :func:`fuzzy_match_drug_type` (backward-compat alias)."""
    # [PERF-13] Fast path for exact match.
    if not isinstance(raw_type, str):
        if raw_type is None or raw_type == "":
            return _DEFAULT_DRUG_TYPE
        # [TEST-9] None / empty / non-string → Unknown.
        logger.debug(
            "_fuzzy_match_drug_type: non-string input %r → %s",
            raw_type,
            _DEFAULT_DRUG_TYPE,
        )
        return _DEFAULT_DRUG_TYPE

    # [SEC-16] Length cap (ReDoS guard for future re-based fuzzy).
    if len(raw_type) > _MAX_DRUG_TYPE_LENGTH:
        logger.warning(
            "_fuzzy_match_drug_type: raw_type length %d exceeds cap %d — "
            "returning %s",
            len(raw_type),
            _MAX_DRUG_TYPE_LENGTH,
            _DEFAULT_DRUG_TYPE,
        )
        return _DEFAULT_DRUG_TYPE

    cleaned = raw_type.strip()
    if not cleaned:
        return _DEFAULT_DRUG_TYPE

    # [PERF-4, PERF-13] O(1) exact match (case-insensitive) — bypass fuzzy.
    exact = _ALLOWED_TYPES_LOWER.get(cleaned.lower())
    if exact is not None:
        return exact

    # [IDEM-3, IDEM-13] Sort alphabetically for deterministic tie-breaking.
    sorted_types = sorted(ALLOWED_TYPES)

    if _FUZZY_AVAILABLE:
        scorer = _get_fuzzy_scorer()
        if scorer is not None:
            try:
                # [DQ-14] Use extract (not extractOne) with limit=2 for tie detection.
                results = fuzz_process.extract(
                    cleaned,
                    sorted_types,
                    scorer=scorer,
                    score_cutoff=_FUZZY_THRESHOLD * 100,
                    limit=2,
                )
                if results:
                    # [CODE-2] extract returns list of (match, score, index) tuples.
                    top = results[0]
                    if len(results) >= 2:
                        second = results[1]
                        # [DQ-14] Tie detection.
                        if abs(top[1] - second[1]) < _FUZZY_TIE_EPSILON:
                            logger.warning(
                                "_fuzzy_match_drug_type: tie between %r "
                                "(score=%.2f) and %r (score=%.2f) for input "
                                "%r — returning first",
                                top[0],
                                top[1],
                                second[0],
                                second[1],
                                cleaned,
                            )
                    # [CODE-2] extractOne returns (match, score, index) in v3+.
                    return top[0]
            except MemoryError:
                # [REL-7] Re-raise MemoryError.
                logger.critical(
                    "_fuzzy_match_drug_type: MemoryError from rapidfuzz — "
                    "re-raising"
                )
                raise
            except Exception as exc:
                # [CODE-11, REL-7] Catch specific exceptions; fall back.
                logger.warning(
                    "_fuzzy_match_drug_type: rapidfuzz error matching %r: %s",
                    cleaned,
                    exc,
                )

    # [DQ-10] INFO for no-match (not DEBUG) — DQ tracking.
    logger.info(
        "_fuzzy_match_drug_type: no match for %r (threshold=%.2f, "
        "scorer=%s), defaulting to %s",
        cleaned,
        _FUZZY_THRESHOLD,
        _fuzzy_scorer_name,
        _DEFAULT_DRUG_TYPE,
    )
    return _DEFAULT_DRUG_TYPE


def fuzzy_match_drug_types(raw_types: Iterable[Any]) -> list[str]:
    """Batch drug-type fuzzy matching (PERF-5).

    Parameters
    ----------
    raw_types : iterable
        Raw drug type strings.

    Returns
    -------
    list[str]
        Matched types in the same order as input.
    """
    return [fuzzy_match_drug_type(rt) for rt in raw_types]


